							<p>In the fourth quarter of FY24, Zomato, a meal delivery platform, recorded a consolidated net profit of ₹175 crore, compared to a loss of ₹188 crore in the same time the previous year. Compared to the December quarter's reported ₹138 crore, the net profit increased by 27%.</p>
