	<p>Any changes made to Google's search engine have an impact. One of the primary ways that people engage with the internet, their computers, and their phones is through the search box. Since Google plays a significant, controversial, and potentially evolving role on the web as a distributor and monetizer of attention, this partial move has been viewed by the media as a watershed moment.</p>
	<p>But after over a year of testing, Google's AI-search experiment hasn't seemed, at least not to me, like a complete makeover—rather, it's just one more questionable result on a results page that gets more and more disorganized. These days, I scan the AI response quickly enough to see that it is occasionally blatantly incorrect. Perhaps it will improve, as Google says; perhaps consumers' enjoyment matters more than quality. In any case, the debate about whether Google is about to completely overhaul the online economy and whether or not synthesized summaries would deliver a last, irreversible blow to publishers and other Google-dependent platforms won't last long.
</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
