	<p>By 2017, Google had stopped using email scanning to target contextual advertising. The company already provided maps, office software, a smartphone operating system, and dozens of other products that relied on user data collecting. At the time, Google was almost living in our pockets, its software on billions of phones, allowing users to conduct an increasing amount of their lives through it, so this gesture toward privacy felt a little out of place.</p>
	<p>Since then, changes in privacy norms have typically happened covertly and with more nuanced effects. When a smartphone user opens their device one day, they discover that their photo library has been sorted into person-based albums after being scanned for faces. Oh. A Zoom user elsewhere discovers that their meeting is automatically being recorded into a searchable manner while having a heated discussion with a coworker. Umm.</p>
	<p>Tech firms have a chance to further change things with AI assistants, which are innovative tools that sometimes appear miraculous and that businesses like Google are eager to sell as such. These tools rely on access to data that is frequently already authorized by users; for example, it's not shocking that a Google assistant requests access to documents stored on Google Docs, but it's also not totally unimportant, and it raises questions about how much the idea of an all-knowing assistant can influence people's expectations regarding their level of digital autonomy.</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
