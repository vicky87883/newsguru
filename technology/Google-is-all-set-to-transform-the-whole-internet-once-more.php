	<p>Similar to how Google was able to begin producing believable results on its own thanks to years of web data collection through Google Search, AI assistants claim to be able to assist you in operationalizing the massive amount of data that Google has been gathering for non-marketing purposes, with your technical consent. From a limited perspective, this seems like a better deal because the customer receives a helpful chatbot in exchange for at least part of the value of the massive personal corpus. On a broader note, though, the appearance of choice ought to sound familiar. (There are indications that Google recognizes and is mindful of privacy issues: It made clear that rather than transferring data to the cloud, its call-screening capability, for instance, would rely on on-device AI.)</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
