	<p>Google has previously used some rather weak justifications to support its claims that gathering user data is essential, such as the need to "help show you more relevant and interesting ads." Its arguments have mostly been presented as software, to which its users have either adopted or rejected them. AI assistants, on the other hand, argue more forcefully for the benefits and necessity of user surveillance. Obviously, if the assistants can see what the user can see, or at least what's on their displays, then they perform better. The conflict that users may experience between a more intelligent and human-like assistant and its demands for access to ever-more private content is lessened by the fact that they are not quite here yet, if and when they do come.</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
