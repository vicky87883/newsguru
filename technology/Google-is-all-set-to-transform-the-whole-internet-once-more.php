	<p>While uncommon, such instances are not unheard of. Google encountered criticism in 2004 for inserting contextual advertisements inside users' inboxes, which at the time was viewed by some as a brazen, arrogant infraction, soon after the debut of Gmail. An organization of privacy groups published an open letter to Google executives stating that "scanning personal communications in the way Google is proposing is letting the proverbial genie out of the bottle." Soon, this would seem both archaic and prophetic. In retrospect, it's obvious that people were content to participate in this transaction to the degree that they could comprehend it, but this was the actual way things were going to operate online.</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
     data-ad-slot="3917672390"></ins>
