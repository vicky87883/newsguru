	<p><mark>Over the last year, the greatest mystery surrounding Google has been related to its main product, which is still its main source of income: can AI chatbots replace search engines? The business provided some clarification in May when it revealed that AI Overviews—Google's new term for AI-generated answers—would soon appear at the top of users' results pages. The video said, "In the next era of search, AI will do the work so you don't have to." It's a step toward a time when the internet only answers questions without offering any links or hints.</mark>
</p>
