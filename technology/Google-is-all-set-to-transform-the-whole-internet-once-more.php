     (adsbygoogle = window.adsbygoogle || []).push({});
</script>
	<p>As the demands of the IT sector are rationally and neatly linked, the widespread belief that the AI surge poses a disruptive danger to the internet companies warrants more skepticism than it has received thus far. According to the people who are developing these companies, the key to realizing the full, glorious potential of large-language-model-based AI, at the level of personal assistants or in the service of achieving machine intelligence, is simply more access to more data. These companies' current businesses were built on the acquisition, production, and monetization of large amounts of highly personal data about their users.
</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
     data-ad-slot="3917672390"></ins>
<script>
     (adsbygoogle = window.adsbygoogle || []).push({});
</script>
	<p>It's more of an idealistic picture of a future where our conventional ideas of what belongs to us have been completely redefined than it is a conspiracy theory or even a calculated scheme. AI companies have claimed that in order to live up to their promises, they must consume massive volumes of both public and private data. A more intimate version of the same claim is being made by Google, which says that it will soon be able to assist with everything. It just asks for everything in return from you.</p>
