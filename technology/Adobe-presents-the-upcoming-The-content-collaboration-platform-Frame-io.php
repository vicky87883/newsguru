<div class=single-excerpt>
	<p><mark>Adobe introduced Frame.io's next iteration, which is currently available to a limited number of users in beta.</mark></p>
	<p><mark>Adobe acquired Frame.io in 2021 and it has been part of Creative Cloud since 2022. (Adobe)</mark></p>
	<p>The fourth iteration of Adobe's Frame.io creative collaboration platform launched on Wednesday with the goal of expediting and enhancing workflows for content production. It will formally launch for Team and Enterprise users later this year, after starting to roll out in Beta for Frame.io Free and Pro customers.</p>
	<p>The goal of Frame.io V4 is to satisfy the complicated demands of the creation of creative material. According to Adobe, disjointed tools and processes cause delays in all stages of development as the demand for creatives, including video material, rises. The newest generation of content collaboration tools is intended to assist creative teams in centralizing input, producing individualized content at scale, minimizing rounds of edits, and expediting the delivery of media assets.</p>
	<p>According to TechCrunch, Adobe purchased Frame.io, an independent product, in August 2021 for $1.275 billion. Later on, it was added as an extension to the business's Creative Cloud service.</p>
	<p>"The demands of the Frame.io community have changed as the demand for all content types—from photographs and videos to designs and documents—continues increasing. No matter the type of creative endeavor, businesses, brands, and people require a single, cohesive platform that simplifies the process of teams and stakeholders coming together to brainstorm, collaborate, and create. Emery Wells, co-founder of Frame.io and vice president of Adobe's Creative Product Group, said in a press release that "V4 is a game-changer for all creatives and production teams - offering the space for greater focus to deliver their best work." All of the new capabilities achieve clear, centralized feedback, fewer revisions, and faster delivery of media assets.</p>
	<p>According to Adobe, more than 4 million users primarily use Frame.io for video review and approval, which helps production teams work more efficiently by facilitating easier communication between video editors and project stakeholders.</p>
