	<p><mark>Although Virat Kohli is leading the IPL 2024 in runs scored thus far and is performing incredibly well, his poor strike rate has drawn criticism.</mark></p>
	<p>Already regarded as one of the greats of Indian cricket, Virat Kohli has never let his supporters down, even after a difficult year in 2021–2022. As the tournament's leading run scorer thus far, Virat Kohli is continuing to dominate the ongoing IPL 2024. According to certain sources, the Board for Control of Cricket in India (BCCI) expressed doubts over Virat Kohli's selection for the T20 World Cup team. However, chief selector Ajit Agarkar made a suggestion that the board may be changing their minds.</p>
	<p>Ajit Agarkar mentioned Virat Kohli's incredible form and the standards he has set for Indian cricket while speaking on the SportifywithPRG podcast. The head selector stated that the BCCI deserves credit for the same and that Indian cricket's future is in capable hands.</p>
	<p>"Observe Virat. He is one of those men who has established standards. After ten to fifteen years in business, he has only become fitter. And the outcomes are seen. It has advanced gradually if someone like him sets an example, suggests things you need, or demands a specific level of fitness," Ajit Agarkar remarked.</p>
	<p>"BCCI has also played a significant role in all of the academy events. You pick things up much faster. The top selector said, "Boys who are 15 to 16 years old today are much fitter than they were, and they should be now with the amount of knowledge, awareness, and facilities available."</p>
	<h5 style="color:red;">Virat Kohli's strike rate concerns</h5>
	<p>As the inaugural batter in the IPL 2024 to smash a century, Virat Kohli secured his place at the top of the list of most centuries in the annals of this wealthy competition. However, immediately after he reached the century mark, there was a lot of talk on social media about how Virat Kohli's century against the Rajasthan Royals was the slowest in IPL history and how his poor strike rate is turning into a big factor in Royal Challengers Bengaluru's (RCB) consecutive losses in the IPL 2024.
</p>
