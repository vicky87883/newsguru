<div class=single-excerpt>
	<p>After "few positive discussions" with the Pakistan Cricket Board, Pakistani bowler Mohammad Amir has decided to change his mind about ending his career. In essence, his choice allows him to participate in the USA and Caribbean T20 World Cup later this year.

"Playing for Pakistan is still my dream!" Amir revealed his choice in a tweet he sent out on Sunday.
</p>
	<p>We occasionally have to reevaluate our choices because life pushes us to that boundary. Following some fruitful conversations with family and well-wishers, I formally proclaim that I am open to being considered for the next Twenty20 World Cup. I have had a few pleasant chats with the PCB, during which they have respectfully informed me that I am needed and can still play for Pakistan. Prioritizing the good of my country before my own desires, I want to accomplish this. My biggest goals have always been and will always be to serve my country while wearing the green jersey, Amir stated.</p>
	<p>Amir thus is the second Pakistan cricketer to take back their retirement after Imad Wasim in the span of a day.</p>
	<p>2020 saw Amir declare his retirement, citing the PCB management as the source of his "mental torture."</p>
	<blockquote class="twitter-tweet"><p lang="en" dir="ltr">I still dream to play for Pakistan!<br><br>life brings us to the points where at times we have to reconsider our decisions, There has been few positive discussions between myself and the PCB where they respectfully made me feel that I was needed and can still play for Pakistan after…</p>&mdash; Mohammad Amir (@iamamirofficial) <a href="https://twitter.com/iamamirofficial/status/1771877982577258795?ref_src=twsrc%5Etfw">March 24, 2024</a></blockquote> <script async src="https://platform.twitter.com/widgets.js" charset="utf-8"></script>
	<p>Amir was embroiled in the notorious 2010 spot-fixing controversy, having intentionally delivered two no-balls at the Lord's Test match against England in exchange for cash. After entering a guilty plea, he was given a five-year playing suspension.</p>
	<p>
Amir, who is currently 31 years old, showed off his skills throughout the most recent Pakistan Super League (PSL) season. Amir represented Pakistan in 36 Tests, 61 ODIs, and 50 T20Is prior to his retirement. 2020 was the last time he played for his nation. The bowler was a member of the Pakistani cricket squad that took home the ICC Champions Trophy in 2017 as well as the ICC T20 World Cup in 2009.</p>
	<p>In the Champions Trophy 2017 final against India, he produced a notably devastating spell that saw Pakistan win the title by dismissing Rohit Sharma, Shikar Dhawan, and Virat Kohli.</p>
