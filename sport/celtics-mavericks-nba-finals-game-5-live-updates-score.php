<p>
The Celtics have won more championships than any other team in NBA history, ending a tie with the Lakers for the most all-time. 
</p>
<p>Throughout the regular season, Boston was the greatest team in terms of offense, defense, and 3-point shooting. This performance continued into the playoffs, particularly in the Finals and particularly in Game 5. 

Boston outscored Dallas with 3-point shooting, going 12-for-23 on threes at one point in the third quarter. They also kept Dallas to fewer than 100 points (on 44.9% shooting) for the fourth time in the series. 
</p>
<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
