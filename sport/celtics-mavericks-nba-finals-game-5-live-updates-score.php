<p>
Luka Doncic resumed Kyrie Irving's horror show at the Garden with 28 points on 12-for-25 shooting, 12 rebounds, and five assists. However, he was just 2 for 9 on 3-pointers. With only 15 points, he was 3-for-9 on threes and 5-for-16 from the field. Give Boston's adaptable defense credit for making it difficult for Doncic and Irving to score.  
</p>
