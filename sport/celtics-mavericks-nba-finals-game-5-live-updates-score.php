<script>
     (adsbygoogle = window.adsbygoogle || []).push({});
</script>
<p>With 9:10 remaining in the third quarter, the Celtics lead 78-52, and the rest of the game was a Boston fan celebration. 

With 31 points, 8 rebounds, 11 assists, and Jayson Tatum in the lead, Jaylen Brown led the Celtics with 31 points, 8 rebounds, and 6 assists. Derrick White scored 14 points and eight rebounds, Jrue Holiday scored 15 points, 11 rebounds, and four assists, and both Celtics starters finished in double figures. 
</p>
<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
