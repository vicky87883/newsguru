<p>
Monday's Game 5 victory by the Celtics against Dallas, 106-88, completed the picture of a champion. 
</p>
<p>
The NBA has a new champion for the sixth straight year as Boston had the best regular-season record (64-18) and cruised through the playoffs with a 16-3 record, never needing more than five games to win a series.
</p>
<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
     data-ad-slot="3917672390"></ins>
<script>
     (adsbygoogle = window.adsbygoogle || []).push({});
