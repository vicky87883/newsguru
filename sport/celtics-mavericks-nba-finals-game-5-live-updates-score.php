<p>At 35, Joe Mazzulla of the Celtics became the youngest coach to win a title since Boston's Bill Russell in 1969. "It was a joy watching the guys just grow as a team throughout the year but also like really work at it," Mazzulla said. "There's a group of guys in the locker room that decided they wanted to win day one, and credit to them."
</p>
<ins class="adsbygoogle"
     style="display:block; text-align:center;"
