<p>There are now seven candidates running for president in 2024; all but Chase Oliver were alive during the administration of President Dwight D. Eisenhower, are free to access 401(k) accounts without penalty, and are qualified to receive Social Security benefits.</p>
<ul>
        <li>Joe Biden, 81</li>
        <li>Donald Trump, 78</li>
        <li>Jill Stein, 74</li>
        <li>Marianne Williamson, 71</li>
        <li>Cornel West, 71</li>
        <li>Robert F. Kennedy Jr., 70</li>
        <li>Chase Oliver, 38</li>
    </ul>
    <p>As the last two commanders in chief have shown, there is a minimum age requirement to run for president, but no upper age limit. During their respective administrations, former President Donald Trump and President Joe Biden both set records for the oldest elected president.</p>
    <div><strong>How old is Donald Trump?</strong></div>
    <p>On Friday, the Republican front-runner celebrated his 78th birthday. He frequently criticizes Biden, implying that the incumbent is unsuitable to be president despite their close age difference of three years.
</p>
<p>
Get ready for the polls by using our voter guide to compare the positions of the presidential candidates on important issues.
</p>
<p>
As previously reported by USA TODAY, Trump's team has continued to use Biden's age as a campaign point, claiming that the oldest president in American history had shown signs of "cognitive decline" while "wandering around" during a photo opportunity at the G7 in Italy.
</p>
<div><strong>How old are the Democratic candidates?</strong></div>
<br>
<img src="assets/imgs/news/biden.JPG" alt="blog" width="100%" height="100%">
<br>
<p>At 81 years old, Biden was born in Scranton, Pennsylvania, on November 20, 1942. He is now the oldest elected U.S. president. He would be 82 years old when he takes office and 86 years old at the conclusion of his term if he wins in November.
</p>
<p>
Biden has to fight low approval ratings, which are worse among younger people, and persuade them that he is more qualified for office than Trump, who is only three years his junior.
</p>
<p>
In the most recent State of the Union speech, the president addressed age-related concerns head-on. "At my age, some things become more obvious than they ever have," Biden remarked.
</p>
<img src="assets/imgs/news/mariana.JPG" alt="blog" width="100%" height="100%">
<p>Born in Houston, Texas on July 8, 1952, best-selling author Marianne Williamson is 71 years old and will be 72 just before the general election on November 5. Less than a month after withdrawing, she announced her long-shot 2024 presidential campaign earlier this year, emphasizing "justice and love."
</p>
<p>
She had earlier suspended it because she was losing "the horse race," but in a statement from February, she said she was returning to oppose Trump's "dark and authoritarian vision," according to Reuters.
</p>
<div><strong>How old are the third-party candidates?</strong></div>
<img src="assets/imgs/news/robert.JPG" alt="blog" width="100%" height="100%">

<p>The age of Robert F. Kennedy Jr. is seventy. He was born in Washington, D.C. on January 17, 1954, making him this year's second-youngest presidential contender. In October 2023, he changed from seeking the Democratic nomination to the independent one.

On October 9, 2023, Robert F. Kennedy Jr. declares his intention to run as an independent in the 2024 presidential contest in Philadelphia, Pennsylvania.</p>
<p>
Kennedy's campaign declared that he will be officially running in a few states, such as Utah, Michigan, and California. However, he still has to fight a difficult and expensive battle to be listed in all 50 states.
</p>
<img src="assets/imgs/news/cornel.JPG" alt="blog" width="100%" height="100%">
<p>The philosopher, scholar, and political activist Cornel West is running as a third-party candidate for president in an attempt to win over progressive, Democratic-leaning voters.
</p>
<p>
West, 71, ran as a Green Party candidate at first, but he later proclaimed himself an independent in October, saying that people "want good policies over partisan politics." He has pledged to provide housing security and eradicate poverty.
</p>
<img src="assets/imgs/news/jilstein.JPG" alt="blog" width="100%" height="100%">
<p>In 2024, 74-year-old doctor Jill Stein, who ran for the Green Party in 2016, plans to run again.

Accusing Democrats of repeatedly breaking their pledges "for working people, youth, and the climate - while Republicans don't even make such promises in the first place" is how she introduced her current campaign.
</p>
<p>
Millions of funds were raised by Stein for recounts following Trump's unexpected 2016 win. Only one electoral analysis in Wisconsin concluded that Trump had won as a result of her accusations.
</p>
<p>Although Kennedy and Trump were invited to speak at the Libertarian Party convention in late May, Chase Oliver, 38, was finally chosen. Oliver received 2% of the vote in his 2022 race for a Georgia state Senate seat.
</p>
<div><strong>What’s the minimum age to run for U.S. president?</strong></div>
<p>A natural-born citizen of the United States must be at least 35 years old to run for president, per the U.S. Constitution. Theodore Roosevelt, who took office at age 42, was the youngest U.S. president to date. Joe Biden, who took office at age 78 and is the only president to have served in their 80s, is the oldest.
</p>
