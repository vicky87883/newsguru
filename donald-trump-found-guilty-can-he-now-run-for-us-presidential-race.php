<p>The US Constitution stipulates that a candidate for president must be at least 35 years old, a citizen of the US, and have been in the nation for 14 years in order to be eligible to run for office. Therefore, his eligibility to run for president will not be affected by a criminal conviction. The former US President may still run for office even if he received a prison sentence prior to the election. A conviction might lose Trump votes in the next elections, according to opinion polls.</p>
<div><strong>Is Donald Trump eligible to cast a vote?</strong></div>
