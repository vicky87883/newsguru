<p>The front-runners in the US presidential contest, Donald Trump and Joe Biden, have found themselves in an unusual predicament this time around before November's general election. Given that one of the front-runners has a criminal record, this highlights a unique situation in US political history.</p>
<p>Despite surviving two impeachments and a plethora of other scandals, it is interesting that Donald Trump leads Joe Biden in the majority of surveys. He was found guilty on May 30 of fabricating company papers as part of a plot to pay a porn actress who claimed they had an encounter hush money in order to illegally influence the 2016 election.</p>
