	<p>China is reducing emissions, but it is doing so for more reasons than just embracing renewable energy. Its output of cement and steel with high carbon content has been declining. The government is spending less on large-scale infrastructure projects after decades of constructing roads and railroads. A protracted boom in the real estate market culminated in a collapse that rocked the economy but reduced emissions. Few economists predict that China's GDP will expand as quickly as it did at the close of the last century and the start of the current one. Stated differently, it is most likely the result of China's dirtiest stage of development.
</p>
	<p>What comes after, though, is much more significant than the top. By 2060, China aims to achieve net zero emissions of greenhouse gases, sometimes known as "carbon neutrality." It will be significantly more difficult to hit this target. Much more than half of China's energy still comes from polluting coal, even after that significant infusion of renewable energy. Although it is less than the roughly 70% it was in 2011, China is nevertheless burning more coal due to rising energy consumption. China permitted the construction of about two new coal-fired power plants each week on average last year, and the country mined a record 4.5 billion tonnes of the black rock.
</p>
	<p>Many of these might never come to pass. The argument for additional building is undermined by the declining rates of operation of the current coal plants. However, China is not weaning itself off of coal as quickly as economists or environmentalists believe is required to accomplish its 2060 aim. The fact that it's widespread in the nation contributes to the issue. Coal is a reliable energy source for China because it uses less gas and oil. Its excavation generates work. Whether or not it is necessary, building a coal plant is another popular strategy used by municipal governments to promote economic development.</p>
