<p class="font-large" style="color:red;">The nation's carbon emissions are about to peak. The challenging part then begins.</p>
	<p>EVEN AS HE lay dying from brain cancer, Tu Changwang wanted to say one final thing. The well-known Chinese meteorologist had noted the warming of the temperature. Thus, he issued a warning in 1961 that this would change the conditions necessary for existence in the Communist Party newspaper, the People's Daily. Nonetheless, he believed that the warming was a natural component of a solar activity cycle that would eventually reverse itself. </p>
	<p>Tu was unaware that the use of fossil fuels was contributing to climate change by releasing carbon dioxide into the atmosphere. A picture of happy coalminers appeared in that People's Daily issue, a few pages ahead of his piece. China was racing toward industrialization in an attempt to close the economic gap with the West.</p>
