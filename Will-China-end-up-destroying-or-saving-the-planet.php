	<h6 style="color:red;">Summits and peaks</h6>
	<p>Thus, there is a chance that China will contribute positively to the climate conference in Dubai. It will not want to appear as though it is ignoring a problem that is at the top of the agenda for many authorities in developing nations, especially if it hopes to lead the global south. The November encounter between China's climate envoy, Xie Zhenhua, and his American counterpart, John Kerry, is another reason for optimism. They also decided to work together on carbon-capture projects, among other minor measures.
</p>
	<p>China has however stated unequivocally that it will not give in to pressure regarding climate change. Its leader, Xi Jinping, reaffirmed earlier this year his goal of attaining carbon neutrality by 2060 and a peak in carbon emissions by 2030. However, he added, "we should and must determine the path, method, pace, and intensity to achieve this goal, and we will never be influenced by others."</p>
