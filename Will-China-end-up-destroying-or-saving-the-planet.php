	<p>Presently, China stands as a global industrial giant, accounting for more than 25% of global manufacturing, surpassing the combined output of Germany and America. However, the advancement has come at an emissions cost. China is the nation that has released the most carbon dioxide into the atmosphere overall over the last thirty years (see chart 1). Today, it contributes more than 25% of the global greenhouse gas emissions annually, as reported by the American research firm Rhodium Group. That is roughly twice as much as the United States, which comes in second (but it is still worse per person).</p>
	<p>Therefore, China plays a critical role in keeping global warming since the Industrial Revolution well below 2°C, as pledged by states in 2015 at the UN climate summit in Paris. The COP28 meeting of this year got underway in Dubai on November 30. For those present, China has both good and terrible news.</p>
	<p>Positively, China's emissions will soon come to an end. According to some analysts, they will peak this year. There's little question that the peak will occur before 2030, China's self-imposed deadline. It is outpacing all other nations in the construction of nuclear power plants. It has also made significant investments in renewable energy (see chart 2), to the point where it now has roughly 750 gigawatts of solar and wind power, or one-third of the global total. The government wants to have 1,200GW of such capacity by the end of the decade, which is more than the current total power capacity of the European Union. China is likely to surpass that goal.
</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
