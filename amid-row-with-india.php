As per a statement issued by the Maldives government, Muizzu emphasized the historical ties between the Maldives and China, tracing back to the ancient Maritime Silk Road. He noted that the archipelago nation became a participant in Xi Jinping's Belt and Road Initiative in 2014.
Speaking about his vision for the expansion of the central airport and commercial port, President Muizzu expressed the government's eagerness to explore partnerships under the framework of the Belt and Road Initiative.
The statement also highlighted Muizzu's acknowledgment of the Free Trade Agreement (FTA) signed between China and the Maldives. He cited the FTA as a significant driver for boosting bilateral trade and investments, particularly by increasing the export of fish products to China. This emphasis marked it as a key priority explored within the FTA.
