	<h2>Diplomatic Row</h2>
	<p>Mr. Muizzu's visit unfolds against a backdrop of internal dissent, with a Maldives Member of Parliament advocating for his removal, and another urging the Parliament to summon the foreign minister. This internal turbulence surfaced after Prime Minister Narendra Modi shared photos of his visit to Lakshadweep on January 4, suggesting that adventure enthusiasts consider adding the archipelago to their bucket list. In response, President Muizzu suspended three ministers for their derogatory remarks.

Amidst these developments, Mariya Ahmed Didi, a former defense minister and a prominent figure in the Maldives' largest opposition party, the MNP, criticized the ruling People's National Congress for potentially straining an "age-old relationship" with India. Didi went further, characterizing India as her country's "911 call."

The repercussions of these strained diplomatic relations manifested as New Delhi summoned the Maldives' envoy on Monday. Furthermore, several Indian tourists, representing the highest arrivals in the island nation in 2023, reportedly canceled their planned trips to the Maldives. The fallout extended to social media, with posts by numerous celebrities and cricketers contributing to #BoycottMaldives trending on Twitter. This unprecedented turn of events underscores the far-reaching implications of diplomatic tensions on tourism and international perceptions.


</p>

