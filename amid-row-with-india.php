<h2>Mr Muizzu is in China for a five-day state visit.</h2>
<hr class="wp-block-separator is-style-wide">
<p>In the face of opposition criticism within his own country regarding his ministers' comments on India and Prime Minister Narendra Modi, President Mohamed Muizzu of the Maldives has characterized China as one of his nation's "closest allies and developmental partners." Currently undertaking a five-day visit to China, Muizzu underscored the significance of this being his inaugural state visit to a foreign nation, highlighting President Xi Jinping's controversial Belt and Road Initiative for delivering "significant infrastructure projects in the Maldives."
While addressing the Invest Maldives Forum in Fuzhou and actively engaging Chinese investors, Muizzu, perceived as having close ties with Beijing, extolled the enduring and fruitful nature of government-to-government and business-to-business cooperation between the two countries.</p>
