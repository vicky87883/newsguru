<p>
Additionally, the Indian Air Force's facility in Hasimara, Bengal, which is home to the IAF's second squadron of sixteen Rafale fighter aircraft, is less than 300 kilometers away from Shigatse Airfield. The duty of guarding Eastern India's Himalayan frontier falls to these Rafales.  </p>
<p>India now has two Rafale squadrons with thirty-six aircraft. It is estimated that China has produced over 250 J-20 stealth aircraft to date. Up until recently, the J-20 was mostly used to protect the Pacific coast on its eastern frontier.  </p>
