<p>
The S-400, which India purchased from Russia in 2018 for ₹ 40,000 crore ($5.43 billion), is an important component of the country's defenses, however it has been demonstrated to be weak while under attack during the conflict in Ukraine. The most current footage shows an S-400 unit, which is made up of radars, missile launchers, and other vehicles, being destroyed by ATACMS ballistic missiles fired by Ukraine that it was obviously unable to detect.
</p>
<p>
Ironically, the S-400 missile system, which is supplied from Russia, is used by both China and India.
</p>
