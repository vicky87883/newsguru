<p>The presence of at least seven J-20s at Shigatse has major military implications for India.</p>
<p>Located at an altitude of 12,408 feet, Shigatse, the second largest city in Tibet, has a dual-use airport for civil and military use. The deployment of the J-20 shows its ability to operate from the harsh environment of ultra-high altitude Tibetan air bases. 
</p>
<p>
The presence of the fighters also indicate that the Chinese Air Force may have deployed infrastructure to sustain operations of the fighters in the area before a possible full-fledged, permanent deployment.
</p>
<p>
The Indian Air Force has declined a request for comment on the significance of the J-20 deployment. </p>
<p>The Indian Air Force faces an obvious and present threat from the J-20's forward deployment in Tibetan airfields. The CEO of NewSpace Research, which is creating drones for the Indian Armed Forces, Sameer Joshi, a veteran fighter pilot, says, "This erodes the advantage enjoyed by IAF assets like the Rafale and imposes caution against the PLAAF [Chinese Air Force]." This capability "clearly negates the numerical advantage in modern jets that the IAF has enjoyed for some time," along with an increase in the number of modern fighters available in the Chinese inventory. 
</p>
<p>As additional J-20s taxi to the flight-line, the photographs included in this report, taken on May 27, depict a J-20 using its drogue parachute to slow down upon landing in Shigatse. In another picture, a J-20 is seen passing two Chinese F-7s, which are licensed versions of Russia's storied MiG-21. In 1964, China started producing the J-7 on its own soil. 2011 saw the first flight of its new J-20 stealth fighter.</p>
<p>"All Source Analysis's Sim Tack, a geospatial analyst, first reported the deployment of the J-20s earlier this week in Shigatse. These aircraft are primarily based in the eastern provinces of China, and the J-20 stealth fighter is China's most advanced operational fighter aircraft to date." "These aircraft are being deployed outside of their usual areas of operation and in close proximity to the Indian border," said the statement upon seeing them arrive at Shigatse in Tibet.
</p>
<p>
The J-20 has already been used in Tibet once before. Between 2020 and 2023, the jets were seen in the Hotan prefecture of Xinjiang, China. However, based on publicly accessible satellite images, this is thought to be the greatest deployment of the J-20.
</p>
<p>According to retired Commodore TJ Maolankar, a former Chief Test Pilot on India's Tejas fighter, which he first touched down on the aircraft carrier INS Vikramaditya, China's deployment of the J-20 at Shigatse is intended to convey a message to India.  
</p>
<p>
From one perspective, it is evident that the Chinese have effectively integrated combat aircraft into their frontline capabilities while operating over the Himalayas. Conversely, it highlights once more the inherent susceptibility of permanent airfields. Is it because they don't think we have the power or the will to attack them first that the Chinese don't mind us flaunting them in this way?"
</p>
<p>From one perspective, it is evident that the Chinese have effectively integrated combat aircraft into their frontline capabilities while operating over the Himalayas. Conversely, it highlights once more the inherent susceptibility of permanent airfields. Is it because they don't think we have the power or the will to attack them first that the Chinese don't mind us flaunting them in this way?"
