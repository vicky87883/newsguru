<p>2017 saw the entry into service of the Chengdu J-20, popularly referred to as the Mighty Dragon, a twin-engine stealth fighter. China became the third nation in the world to operationally field stealth jets with the introduction of the fighter. The aircraft is always being updated, and it has several sensors installed. Its main function is to combat air superiority, and it is equipped with China's most sophisticated air-to-air missiles, such as the PL-15 long-range missile, which is thought to be capable of hitting targets up to 300 kilometers away.</p>
<p>''Deploying quickly over large swathes of Tibet in a fused awareness spectrum, helping operationalise for the first time, a 'intelligentised' large area defensive doctrine for the PLAAF,'' says Sameer Joshi of the J-20's ability to work in tandem with drones, electronic warning platforms, and airborne warning aircraft.  To put it simply, this means that the J-20 can function as a vital component of a highly networked and electronically enabled force that makes use of a variety of new aviation assets, such as cutting-edge drones, fighters of the latest generation, and older assets that are electronically connected through data-links to allow the force to deploy as a unified warfighting unit.

</p>
<p>Over the past five years, China has gradually increased its air power capability in Tibet and other regions close to India. According to Sim Tack, this has mostly involved building new air bases and modernizing the facilities of already-existing air bases. At least temporarily, China has also started sending planes like its J-20 and H-6 nuclear-capable bombers to these border areas. 
</p>
<p>
In response to China's airfield expansion, India has upgraded its own airbases considerably, adding hardened shelters for its aircraft and strengthening its tier-by-tier surface-to-air missile defense system, which includes the S-400 long-range surface-to-air missile system, which was built in Russia, stationed in Eastern India. 
</p>
<p>The goal of the S-400 system's deployment along the Line of Actual Control is to prevent aggressive Chinese fighter-sweeps. It is purported to be able to track stealth platforms. 
</p>
