	<p>Former President Donald Trump unexpectedly softened his position on immigration and stated that he would want to give green cards to foreign graduates of US institutions automatically. On an appearance on the "All-In" podcast, the president stated, "What I want to do and what I will do is, you graduate from a college, I think you should get a green card automatically as part of your diploma, a green card to be able to stay in this country." He expanded this suggestion to cover doctorate programs and graduates of two-year junior institutions.    </p>
