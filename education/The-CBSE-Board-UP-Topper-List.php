	<p>Results of the CBSE Board केंद्रीय माध्यमिक मोर्ड ने 2023–24 के लिए सीनियर सेक्ं्ाी (कक्षा 12) के नतीजे जारी कर दिए हैं।. Results.cbse.nic.in पर नतीजे घोषित हैं।  रिजल्ट चेक करने के लिए आपको वेबसाइट पर लॉग इन करना होगा। यहां अपना रोल नंबर डालकर इंटर करें। इसके बाद आप अपना रिजल्ट चेक कर सकते हैं।</p>
	<p>डिजिटल डेस्क, नई दिल्ली। (सीबीएसई बोर्ड टॉपर्स की सूची) 2023-2024 के लिए माध्यमिक (कक्षा 10) और वरिष्ठ माध्यमिक (कक्षा 12) परिणाम केंद्रीय माध्यमिक शिक्षा बोर्ड द्वारा सार्वजनिक कर दिए गए हैं। निष्कर्ष बोर्ड की आधिकारिक वेबसाइट पर उपलब्ध हैं। एशियन सीनियर सेकेंडरी स्कूल, शिकोहाबाद में पढ़ने वाली फिरोजाबाद की इंटरमीडिएट की छात्रा गुनगुन को संभावित 100 में से 97.7 अंक प्राप्त हुए हैं। सीबीएसई 12वीं कक्षा में, दिल्ली पब्लिक के लक्ष्य भारद्वाज स्कूल, आगरा रोड को संभावित अंकों में से 99.2 प्रतिशत अंक प्राप्त हुए।</p>
	<p>आगरा से सुमित राहुल पब्लिक स्कूल की 12वीं साइंस की छात्रा खुशी अग्रवाल ने 98.6 प्रतिशत अंक हासिल किए हैं।</p>
	<h5 style="color:red;">मुजफ्फरनगर से रितिक ने किया टॉप</h5>
	<p>रितिक चौधरी, जिन्होंने सीबीएसई 12वीं कक्षा की परीक्षा के दौरान अपने जिले में प्रथम स्थान प्राप्त किया था, एक आईएएस अधिकारी बनने और अपने देश की सेवा करने की इच्छा रखते हैं। असाधारण छात्र के कंप्यूटर विज्ञान, चित्रकला और अंग्रेजी में उत्तम अंक थे। मां नीलू सिंह आर्य एकेडमी इंटरनेशनल स्कूल में पढ़ाती हैं, जबकि पिता नितिन कुमार किसान हैं।</p>
	<h5 style="color:red;">इस तरह चेक करें रिजल्ट</h5>
	<p>सीबीएसई बोर्ड ने अपनी आधिकारिक वेबसाइट results.cbse.nic.in और cbse.gov.in पर नतीजे घोषित कर दिए हैं। रिजल्ट चेक करने के लिए आपको वेबसाइट पर लॉग इन करना होगा। यहां अपना रोल नंबर डालकर एंटर करें। इसके बाद आप अपना रिजल्ट चेक कर सकते हैं.</p>
	<p>डिजिलॉकर के जरिए कैसे चेक करें CBSE Board Result</p>
