	<p>MUMBAI: At least eight students from IIT-Bombay, some of whom are graduating and others are juniors, have been fined up to Rs 1.2 lakh for putting on a play called Raahovan on March 31 during the school's annual performing arts festival.
Some students complained that the performance, which was largely based on the Ramayana, portrayed the main characters in a demeaning way.
</p>
