<p>According to a student, junior students have been forced to pay Rs 40,000 apiece and have been denied access to dorm facilities, while graduating students have been fined Rs 1.2 lakh each and will not be eligible for any gymkhana rewards. Following the allegations, the institute acted on the recommendations of a disciplinary action committee.</p>
