<p>IIT-Bombay hosts the Performing Arts Festival, or PAF, every year as a cultural event. This year, it took place in March, and on March 31, the performance was presented at the campus outdoor theater. In the days that followed, a video showcasing play excerpts juxtaposed with Ramayana facts went viral, igniting a discussion over artistic liberty and offending religious sensibilities.</p>
