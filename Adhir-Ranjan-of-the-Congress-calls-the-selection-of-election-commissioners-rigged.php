                                that the statute was changed to make sure the CJI was excluded from the selection
                                procedure.As per the recently enacted legislation, the selection procedure comprises of
                                two committees: a search committee comprising three members, headed by the Union law
                                minister and comprising two government secretaries; and a selection committee of three
                                members, headed by the Prime Minister and comprising a Union minister on recommendation
                                from the PM and the opposition leader.</p>
