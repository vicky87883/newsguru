                            <p>The lone opposition member of the panel, Chowdhury, criticized the selection procedure
                                for the new hires, claiming it was "fait accompli" that the applicants selected by the
                                government would be appointed to the positions.Speaking to reporters at his house
                                shortly after the meeting concluded on Thursday, Chowdhury voiced his disapproval of the
                                selection procedure, alleging that he had only been provided a list of the six
                                shortlisted candidates ten minutes prior to the start of the panel discussion.He claimed
                                that only last night, as the opposition member, he was handed a list of 212 names to
                                review. "You tell me if it is humanly possible for me to look through all 212 names and
                                identify the best qualified individual among them in a single night. "It was
                                inevitable," he remarked.</p>
                            <p>The Chief Justice of India ought to have been on the selection panel, according to
                                Chowdhury, who also expressed confusion over the process used to narrow the list of six
                                names.</p>
