<p>In response to the Congress president Mallikarjun Kharge and general secretary Jairam Ramesh uploading a “twisted, distorted” version of one of his interviews on the microblogging platform X, Union Minister Nitin Gadkari submitted a legal notice to both parties on Sunday.

In an allegation that the Congress "concealed the contextual intent and meaning" of his interview when it published a 19-second clip, Gadkari said that the Congress's "sinister act" was carried out purely to "fan and create confusion, sensation and disrepute" about him.</p>
	<p>Gadkari stated that the Congress leaders' "intentional attempt" to "insult and demean" him was demonstrated by the "factually incorrect" film, "coupled with the intent to provoke members of the BJP to develop ideological rift."
In addition, he asserted that the video had caused "huge reputational harm, defamation, and great loss of credibility."

The BJP leader's notification stated, "The interview has also been twisted, distorted, and presented on your microblogging site 'X' wall by uploading the above video which is bereft and devoid of contextual meaning."

Then, Gadkari wanted a written apology from Congress within three days and urged that the post be removed "within 24 hours of receipt of this legal notice."
</p>
