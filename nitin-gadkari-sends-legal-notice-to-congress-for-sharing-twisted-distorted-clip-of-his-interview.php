	<p>Nitin Gadkari was heard stating, "Villages, poor, laborers and farmers are unhappy," in the video that the Congress released.There are poor roads, unsafe drinking water, inadequate hospitals, and inadequate schools in the villages.</p>
	<p>The Union Minister of Road Transport and Highways has charged that Congress has omitted passages from his speech regarding the efforts being made and the positive results they have seen under the current Union Government's tenure.

Gadkari claimed, "(They) posted the Hindi captions and video by hiding the contextual meaning of the interaction, which is malicious and purposeful to damage the minister's reputation."

Congress has not yet replied to the notification.</p>
