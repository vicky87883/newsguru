<p>"My thoughts are with the people impacted by the storms in West Bengal's Jalpaiguri and Mainaguri regions. I send my condolences to the families of the deceased," Modi stated in a post on X, Modi.
The prime minister said, "I would also urge all @BJP4Bengal Karyakartas to assist those affected."
</p>
