<p>In two locations in the Jalpaiguri district of north Bengal on Sunday afternoon, heavy norwester damage severely damaged dwellings, resulting in at least five fatalities—including one involving a woman—and around 500 injuries. On Sunday evening, Chief Minister Mamata Banerjee arrived in Jalpaiguri after postponing her planned events. In addition, she assessed the situation and spoke with those impacted by the typhoon at Jalpaiguri Super Specialty Hospital.</p>
<p><mark>The storm, which lasted for around ten minutes, struck settlements across a few square kilometers at 3:30 p.m.</mark></p>
<p>Speaking to the media, Mamata Banerjee claimed that a calamity had happened, resulting in five fatalities and damage to many houses. There are two more in critical condition.
"The administration is responding quickly and offering the support that is required. The chief minister gave an assurance that the administration is making every effort to assist the victims.
</p>
<ins class=adsbygoogle style=display:block;text-align:center data-ad-layout=in-article data-ad-format=fluid data-ad-client=ca-pub-6958761602872755 data-ad-slot=3917672390></ins>
<script>(adsbygoogle=window.adsbygoogle||[]).push({});</script>
<p>"The government will support those in need. We are conscious of the harm that has occurred. The loss of life is the worst harm that has occurred, according to Mamata Banerjee.
She expressed her gratitude to the administration for their efforts in disaster management.
"The hospital staff, including the physicians and nurses, is doing a great job of handling the issue. The rescue efforts have already concluded," she continued.
</p>
<p>It was reported that Jalpaiguri Medical College and Hospital admitted up to 170 injured patients. About 200 people were sent to the Maynaguri Hospital, and another 100 people were receiving care at the Barnesh Health Center.
Although no casualties were reported, similar storms also affected other areas of the nearby districts of Aliupurduar and Cooch Behar.
<mark>Prime Minister Narendra Modi expressed his condolences earlier on Sunday for the deaths caused by a storm that caused havoc in some areas of the Jalpaiguri region. Modi claimed to have met with officials and asked them to make sure individuals impacted by it receive the necessary support.</mark>
</p>
<ins class=adsbygoogle style=display:block;text-align:center data-ad-layout=in-article data-ad-format=fluid data-ad-client=ca-pub-6958761602872755 data-ad-slot=3917672390></ins>
