    <p>This transfer is a component of the two countries' greater continuing defense collaboration, which is centred on India's "make in India" strategy in the defense sector. The Indian Army uses the modernized AK-203 assault rifle, which is chambered for the 7.62×39mm cartridge. It is anticipated to improve the army's combat capability.
    </p>
    <p>IRRPL is a joint venture between the Ordnance Factory Board of India and the Kalashnikov Group and ROSOBORONEXPORT JSC, two companies of Rostec State Corporation, in Russia. By means of the state defense order, Rostec is Russia's principal provider of weapons, military hardware, and specialized equipment.
    </p>
    <ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
     data-ad-slot="5307231561"></ins>
<script>
     (adsbygoogle = window.adsbygoogle || []).push({});
</script>
    <p>The project is a component of the Self-Reliant India (Atmanirbhar Bharat Abhiyaan) initiative in India, which aims to localize defense production. The significance of this endeavor was emphasized by Rostec Director General Sergey Chemezov, who stated, "Today, India is the first foreign customer to produce the 'two hundredth' series of Kalashnikov assault rifles." In complete accordance with the Made in India and Atmanirbhar Bharat Abhiyaan initiatives, production has been set up in India."
    </p>
    <p>The rifles are made in the Korwa Ordnance Factory in Amethi, Uttar Pradesh, which has all the equipment needed to create the guns and guarantee prompt delivery.
    </p>
    <p>The project's successful execution was emphasized by Alexander Mikheev, Director General of ROSOBORONEXPORT, who stated, "This made it possible to produce and deliver a batch of 35,000 Kalashnikov assault rifles to the Indian Army within the timeframe agreed with the Ministry of Defence of India." A "vivid example of fruitful cooperation between our countries in the defence sector" was another way Mikheev described the joint company.
    </p>
    <ins class="adsbygoogle"
     style="display:block; text-align:center;"
