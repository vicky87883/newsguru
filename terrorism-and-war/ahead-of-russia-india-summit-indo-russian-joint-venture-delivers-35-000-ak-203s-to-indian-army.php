    <p>This development comes with Rostec's announcement that "Mango shells" will now be manufactured in India. The project is being carried out under the Made in India initiative, and the weapons technology is made to combat armored vehicles equipped with composite armor. The T-72 and T-90 MBTs are equipped with weapons that can fire the 125mm Mango round.
    </p>
    <p>The foundation of Indo-Russian relations continues to be defense cooperation, with multiple joint initiatives over the previous few years. Defense links are still strong and expanding, ranging from S-400s to the licensed manufacturing of T-90 tanks and Su-30 MKI, supply of MiG-29 and Kamov helicopters, INS Vikramaditya (previously Admiral Gorshkov), production of BrahMos missiles and Ak-203 rifles in India. Russia also approved the logistical agreement between India and Russia before to the meeting, which improved military cooperation between the two countries.
    </p>
