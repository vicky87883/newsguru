	<p><mark>The Russian side has declared that the Indo-Russian weapons Private Limited (IRRPL) has built and given the Indian Army around 35,000 "made in India" Kalashnikov AK-203 assault weapons ahead of the 22nd India-Russia summit.
    </mark></p>
    <ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
