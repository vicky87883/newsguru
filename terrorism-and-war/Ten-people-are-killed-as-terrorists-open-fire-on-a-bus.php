<div class=single-excerpt>
	<p><mark>Terror Attack In J&K: According to IANS, there were at least ten fatalities in Jammu and Kashmir on Sunday as a result of an alleged terror attack on a bus. When the tragedy in the Poni region occurred, the pilgrimage vehicle was returning to Katra town from the Shiv Khori shrine.
       </mark> 
</p>
<p>
According to preliminary accounts, gunmen opened fire on a bus carrying pilgrims from Katra town in the Reasi area back to the Shiv Khori temple.
</p>
<p>After being fired at by suspected terrorists, the bus lost control and fell into a ravine. As soon as possible, army, police, and paramilitary personnel raced to the location to begin rescue and relief efforts.
</p>
<p>
Mohita Sharma, the senior superintendent of police in Reasi, told PTI that nine pilgrims had died and thirty-three had been injured. 
</p>
