<p>Sharma remarked, "It is a very unfortunate incident," in an interview with ANI regarding the bus tragedy. According to the reports we first received, after the terrorists opened fire on the passenger bus, the bus was the target of a terrorist strike."
</p>
<p>
The SSP added that the driver of the bus lost control and it crashed into a ravine when the terrorists opened fire on it as it was traveling to Katra. The victims have been referred to hospitals in the Reasi District and Naraina, he continued, and the rescue efforts are now complete. 
</p>
<p>
The passengers weren't locals, Sharma informed ANI, but their names are yet unknown. Sharma stated that the commuters are from Uttar Pradesh, citing the results of the primary inquiry.
</p>
