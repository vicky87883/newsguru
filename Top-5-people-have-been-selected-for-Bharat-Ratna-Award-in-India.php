<p>MS Swaminathan, who was born in 1925, is regarded as the pioneer of the Green Revolution in India.

Both the Coimbatore Agricultural College and the Maharajas College in Thiruvananthapuram awarded him a Bachelor of Science degree in Agricultural Sciences and Zoology, respectively.

Additionally, he received his PhD from Cambridge University in the UK and his MSc in Agricultural Sciences from the Indian Agricultural Research Institute (IARI), with a focus on genetics and plant breeding.
	</p>
