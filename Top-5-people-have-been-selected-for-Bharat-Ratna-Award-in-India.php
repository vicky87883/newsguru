<p class=font-large>Usually in the previous year, only 3 people have been selected for Bharat Ratna But this year 5 people have been selected for this Honorable award.</p>
	<p>The prime minister of India announced that PV Narasimha Rao, Charan Singh, MS Swaminathan, LK Advani, and Karpoori Thakur have been selected for Bharat Ratna.</p>

<p>New Delhi: Today, Prime Minister Narendra Modi announced his intention to award the esteemed Bharat Ratna, the country's highest civilian honour, to eminent agricultural scientist MS Swaminathan together with former prime ministers PV Narasimha Rao and Chaudhary Charan Singh.</p>

<p>Three people are traditionally awarded the Bharat Ratna each year. This year is different, though, as the government has decided to honor the services of five outstanding individuals. In addition to Rao, Singh, and Swaminathan, former Bihar chief minister Karpoori Thakur and seasoned BJP leader LK Advani have been chosen for this prestigious award.</p>
