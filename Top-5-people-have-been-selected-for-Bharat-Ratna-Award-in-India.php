<p>
PV Narasimha Rao served the position of prime minister between June 21, 1991, and May 16, 1996.

He attended Osmania University in Hyderabad, Bombay University, and Nagpur University after being born in Karimnagar, Andhra Pradesh, in 1921. 

In addition to being an advocate and an agriculturist, he was Andhra Pradesh's chief minister.

In addition, he served as the Minister of Home Affairs from July 1984 to December 1984, the Minister of Defence from December 1984 to September 1985, and the Minister of External Affairs from January 14, 1980, to July 18, 1984.</p>
	<p>
		
		<b>2). Charan Singh</b></p>
	<figure class="single-thumnail mb-30">
<img src=assets/imgs/news/charansingh.webp alt="blog" width="60%">
