<p>
Charan Singh, who was born in Meerut, Uttar Pradesh, in 1902, held the position of prime minister from July 28, 1979, to January 14, 1980.

From Chhaprauli, he was initially elected to the Uttar Pradesh Legislative Assembly in 1937. The constituency was represented by him in 1946, 1952, 1962, and 1967.

In addition, he led Uttar Pradesh as chief minister twice, in 1967 and 1970.
	</p>
