							<p><mark>A fire on one of the engines of the Bengaluru-bound Air India Express aircraft was discovered shortly after takeoff, necessitating an emergency landing at the airport.</mark></p>
							<p>On May 18, an Air India Express aircraft from Bengaluru to Kochi was forced to make an emergency landing in Bengaluru due to an engine fire, according to PTI, a news agency.</p>
							<p>The Air India Express issued a statement that read, “All the passengers and crew were evacuated, and no one was injured." </p>
							<p>According to a representative for Air India Express who talked to PTI, "The crew completed an evacuation with no guests injured." We are sorry for any trouble this has caused, and we are making other plans to get our passengers to their destination as quickly as possible."</p>
							<p>"On May 18, 2024, IX 1132 from Bengaluru to Kochi made an emergency landing at BLR Airport at 2312 hrs, due to a reported fire in one of the engines," a representative of Bengaluru International Airport Limited (BIAL) stated in a statement, according to PTI.</p>
							<p>The fire was noticed minutes after the Kochi-bound Air India Express flight took off from Bengaluru, according to media sources. This reportedly prompted an emergency landing.</p>
							<p>As soon as the fire was discovered, the air traffic controller was notified. A full-scale emergency was subsequently proclaimed.
							</p>
							<p>
The Kempegowda International Airport (KIA) in Bengaluru was home to emergency fire control units, who quickly extinguished the fire following the emergency landing. Bengaluru International Airport Limited (BIAL) is a special purpose corporation that was established by the Karnataka State Government in January 2001 to oversee Bangalore International Airport (KIA).
</p>
							<p>The BIAL spokesperson further informed PTI that a full-scale emergency was declared and all 179 passengers and six crew members onboard were successfully evacuated from the aircraft.</p>
							<p>An Air India Express spokeswoman stated that the Bengaluru-Kochi aircraft 'decided' to return and made a precautionary landing in Bengaluru as a result of possible flames from the right engine after takeoff. Additionally, the ground services reported fires, which aided in the flight's evacuation.

