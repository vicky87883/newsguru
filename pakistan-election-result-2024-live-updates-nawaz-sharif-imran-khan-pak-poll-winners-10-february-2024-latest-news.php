	<p>The National Assembly's 250 seats have been counted, and the independent candidates—the great majority of whom were supported by the imprisoned former prime minister Imran Khan's Pakistan Tehreek-e-Insaf (PTI) party—were in the lead with 99 seats, according to PTI, which cited the Election Commission of Pakistan.</p>
<p>
The Pakistan Muslim League-Nawaz (PML-N), led by former prime minister Nawaz Sharif, has won 71 seats; the Pakistan Peoples Party (PPP) has won 53; the Muttahida Qaumi Movement has won 17; and smaller parties have won the remaining seats.</p>
<p>
After the PTI-backed Independent candidates' victories upset Sharif's plans and the security establishment's support, he announced on Friday that he was going to form a coalition government. A day before, after casting his vote, Sharif had categorically rejected the idea of forming a coalition and declared his preference for one party to lead Pakistan for a complete five-year term.</p>
