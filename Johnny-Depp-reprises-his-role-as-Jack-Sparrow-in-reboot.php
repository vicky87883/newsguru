	<p>Upon being questioned about Johnny Depp's possible participation in the reboot, Bruckheimer expressed his desire for the actor to reprise the part, saying, "It's a reboot, but if it were up to me, he would be in it. I cherish him. He is a trustworthy companion. He has a distinct style and is an incredible artist. Captain Jack was made by him. That wasn't on the page; he was channeling Keith Richards and Pepé Le Pew. That was how he understood Jack Sparrow.</p>
