	<p>Two Pirates films were reportedly in early production in 2020, according to Variety: a remake starring Margot Robbie and the sixth installment in the original series, which was scripted by Craig Mazin and Ted Elliott. Speaking with the Los Angeles Times in 2023, Mazin talked about the sixth film and recalled the original proposal, saying, "We pitched it and thought there's no way they're buying it, it's too weird." Indeed, they did! After that, we wrote a great screenplay, the strike occurred, and everyone was left waiting.</p>
	<p>Jerry Bruckheimer said to Entertainment Weekly that both projects may go forward, despite earlier rumors that Disney wasn't interested in Margot Robbie's female-led version. He said, "It's two different movies." We intend to produce them both, and I believe Disney concurs that they truly want to do the Margot one as well.</p>
