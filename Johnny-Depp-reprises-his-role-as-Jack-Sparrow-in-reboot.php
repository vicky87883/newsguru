	<p>Fans all around the world have grown to appreciate Johnny Depp's famous depiction of Captain Jack Sparrow in the Pirates of the Caribbean film series. Due to Depp's distinctive take on the role, Jack Sparrow has become one of the most recognizable characters in contemporary film. The popular franchise's producer, Jerry Bruckheimer, has stated that he hopes to see Depp return to the iconic character of Captain Jack Sparrow in the next reboot. From 2003 until 2017, he played the adored role in the popular series. However, after his 2022 libel lawsuit against his estranged wife Amber Heard, his future participation in the franchise has been dubious.
