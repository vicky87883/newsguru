	<p>The liver was donated to a 34-year-old woman who was brought to ILBS Hospital in New Delhi since cross-matching failed to find a suitable recipient for the organ at PGIMER, according to Dr. Koushal.
	</p>
	<p>
In order to guarantee the secure and efficient transportation of the harvested organs, a green corridor was established in tandem with the retrieval schedule, starting at PGIMER and ending at the international airport in Mohali. This allowed for the safe transit of the recovered liver to Delhi on Friday.
	</p>
	<p>
Two men who were recipients of kidney transplants were a 39-year-old man who had been on dialysis for a long time and a 34-year-old man who had type 1 diabetes. Both men received a new lease on life after undergoing simultaneous kidney and pancreas transplants.
</p>
	<p>Two people who were blind from the cornea were able to see again thanks to the donated corneas. Thus, Shubham's family's kind decision to donate his organs has affected five lives, according to Dr. Koushal.
</p>
