	<p>Five patients at PGIMER, Chandigarh, received new leases of life thanks to the organ donation of a 24-year-old brain dead Haryana youngster.
</p>
	<p>On May 7, Shubham, a student from Kaithal, Haryana's Patti Afgan village, was hurt in a car accident. The next day, he was taken to PGIMER due to severe injuries.
</p>
	<p>Medical professionals tried to save Shubham, but he was deemed brain dead on May 9 following two appropriately called meetings of the Brain Death Certification Committee.
	</p>
	<p>
Transplant coordinators addressed the family to discuss the possibility of organ donation in accordance with the Transplantation of Human Organs Act (THOA), and with their approval, the procedure was started.
	</p>
	<p>
In describing the situation, Dr. Vipin Koushal, the nodal officer for the Regional Organ and Tissue Transplant Organization (ROTTO) and medical superintendent at PGIMER, stated, "With the family's consent, we secured Shubham's liver, kidneys, pancreas, and corneas."
</p>
