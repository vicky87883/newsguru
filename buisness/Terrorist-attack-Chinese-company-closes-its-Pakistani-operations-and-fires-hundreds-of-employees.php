	<h6 style="color:red;">NEW DELHI:</h6><p>In the wake of a devastating suicide attack that left five Chinese nationals dead, a Chinese company has suspended civil construction and fired hundreds of workers at a hydroelectric project in Pakistan.
Six persons were killed in an explosion at the Dasu hydroelectric project in the province of Khyber Pakhtunkhwa when an explosives-laden vehicle targeted a personnel bus.</p>
	
	<p>Since 2021, there have been two suicide attacks in the area targeting people connected to a project financed by China.
In light of the security concerns raised by the recent attack, Power Construction Corporation of China (PCCC), the company in charge of the Tarbela 5th Extension Hydropower Project in the same province, decided to halt operations and fire over 2,000 employees, Dawn reported, citing official sources.
</p>
	<p>The general secretary of the Awami Labour Union at the project promised that there would be little delay in the project's completion date despite the suspension of work. The World Bank and the Asian Infrastructure Investment Bank are providing financial support for the Tarbela expansion project (T5), which is expected to be completed by May 2026. According to labor standards, the laid-off employees are entitled to half of their salary until they are called back to resume their jobs.</p>
	<p>The deadly incident took place at the Dasu hydropower project, which is being developed by China Gezhouba with World Bank support and is located around 300 kilometers north of Islamabad. The Chinese nationals engaged in this project have already been the target of similar attacks in July 2021, which claimed the lives of both Chinese and Pakistani laborers.
China, meanwhile, urged an investigation into the tragedy and harshly denounced a suicide bombing.
</p>
	<p>Shehbaz Sharif, the prime minister of Pakistan, has directed a comprehensive cooperative inquiry to find the culprits and protect the security of Chinese workers engaged in numerous projects throughout the nation. Chinese laborers in Pakistan are a result of the multibillion dollar China-Pakistan Economic Corridor (CPEC), which aims to improve connectivity and economic cooperation between the two nations.
</p>
