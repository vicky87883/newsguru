	<p>Judge Khanna, who was seated separately from Justices M.M. Sundresh and Bela M. Trivedi, made the statement in response to a request for clarification regarding the listing of Kejriwal's plea by Additional Solicitor General S.V. Raju, who was representing the Center in a case involving the Goods and Services Tax.

The leader of the Aam Aadmi Party (AAP) was taken into detention on March 21 and is presently being held in judicial custody at Tihar Jail.


The Bench, which also included Justice Dipankar Datta, had given Mr. Kejriwal till May 7 to get a decision regarding interim bail.
</p>
