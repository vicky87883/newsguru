	<p>The two-judge bench had not yet issued an order allowing Mr. Kejriwal to be granted temporary release so he could run in the Lok Sabha elections.

Following hearing the arguments from Senior Advocate Abhishek Singhvi and Additional Solicitor General S V Raju, who represented Mr. Kejriwal and the Enforcement Directorate, respectively, the Bench had postponed making a verdict.

Due to the Lok Sabha elections, Solicitor General Tushar Mehta, who also represented the inquiry agency, had adamantly resisted extending any leniency to Mr. Kejriwal. He claimed that providing interim bail to the AAP national convenor would be equivalent to creating a class apart for politicians.
</p>
	<p>The hearing on Mr. Kejriwal's request to be released from custody has been broken into two sessions by the Bench.

His primary lawsuit challenges the legality of his ED detention and asks that it be ruled unlawful; the second part of the case concerns the granting of temporary bail in light of the upcoming Lok Sabha elections.


On the question of granting temporary bail, the court postponed making a decision.
</p>
