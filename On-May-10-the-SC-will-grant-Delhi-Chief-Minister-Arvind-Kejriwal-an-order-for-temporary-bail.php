	<p>In the money laundering case, a Delhi court on Tuesday prolonged Mr. Kejriwal's judicial custody till May 20.

On April 9, the Delhi High Court affirmed Mr. Kejriwal's arrest, ruling that there was no illegality and that the ED had "little option" because he had ignored several summonses and failed to cooperate with the inquiry.

The issue concerns purported corruption and money laundering during the creation and implementation of the excise policy for 2021–2022 that the Delhi government ultimately decided to abandon.
</p>
