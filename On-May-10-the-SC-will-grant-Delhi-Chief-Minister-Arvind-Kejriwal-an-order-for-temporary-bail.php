<p class=font-large><mark>On May 10, the Supreme Court is expected to issue an order granting Delhi Chief Minister Arvind Kejriwal interim release in a money laundering case related to the purported excise policy scandal.</mark></p>
	<p>The Bench led by Justice Sanjiv Khanna, who presided over the hearing of Mr. Kejriwal's appeal challenging his arrest in the case, declared on Friday that an interim order (on temporary bail) will be issued. On the same day, the primary issue regarding the arrest challenge will also be heard.</p>
