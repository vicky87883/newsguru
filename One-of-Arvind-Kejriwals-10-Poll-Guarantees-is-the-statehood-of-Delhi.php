	<p>
Mr. Kejriwal claims that other assurances on the list include "freeing" Indian land from Chinese control, ending the Agniveer program, giving farmers MSP in accordance with the Swaminathan Commission findings, and giving Delhi complete statehood. 
</p>
	<p>"'Nation first' is our fourth guarantee. While our central government denies it, China has been occupying our territory.The might of our army is immense. Every inch of the nation that China has taken over will be returned to its original owners. On one hand, diplomatic efforts will be made in this respect, while on the other, the Army will be granted total autonomy to pursue its interests in this regard. Agniveer is a bad program that troubles young people as well as the army. Mr. Kejriwal declared, "The Agniveer scheme will be withdrawn." 
</p>
	<p>The Delhi excise policy scam case led to Mr. Kejriwal's detention by the Enforcement Directorate (ED). On Friday, the Supreme Court granted him temporary release. </p>
