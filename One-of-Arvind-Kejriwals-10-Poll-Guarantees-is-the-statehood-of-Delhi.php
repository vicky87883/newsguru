	<p>Arvind Kejriwal, the chief minister of Delhi, declared today the '10 assurances' offered by the AAP in the event that the party wins the 2024 Lok Sabha elections. Mr. Kejriwal's commitments cover a wide range of public welfare projects, like as promises of improved healthcare and free power.</p>
	<p>
"BJP has always failed on its promises; my guarantees have proven track record," stated Kejriwal. "People have to take a call whether to go for 'Kejriwal ki Guarantee' or 'Modi Guarantee."
</p>
	<p>The guarantee of free power for everyone is at the center of Mr. Kejriwal's election campaign pledges, with an emphasis on the underprivileged segments of society. The leader of the AAP argued for a national rollout of Delhi's continuous power supply paradigm. 
</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
