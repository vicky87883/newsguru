	<p>"The first of the ten promises is that the nation would have power available around-the-clock. Although the nation can produce 3 lakh MW of power, only 2 lakh MW are actually used. More power can be produced in our nation than can be used. We will carry it out nationwide, like we have in Punjab and Delhi. We will provide all the impoverished people up to 200 units of free power. We can make arrangements for the ₹ 1.25 lakh crore expenditure," Mr. Kejriwal stated. 
</p>
	<p>Restructuring education in public schools is second on Mr. Kejriwal's list of electoral promises. The chief minister of Delhi asserted that ₹ 5 lakh crore would be needed to fix it, and that the federal government and state governments should each contribute ₹ 2.5 lakh crore to cover the entire expense. </p>
<p>
"The state of our government schools is not excellent right now. Our second promise is that we will set up first-rate, free education for everybody. A superior education will be given by government schools than by private ones. We've completed it in Punjab and Delhi. It will cost ₹ 5 lakh crore to do this. For this, the national government would provide ₹ 2.5 crore and state governments will contribute ₹ 2.5 lakh crore," he stated. 
</p>
	<p>The AAP national convener also proposed spending ₹ 5 lakh crore to upgrade India's healthcare system and restructure government institutions. 
</p>
	<p>"The state of our nation's government hospital is not excellent. Improved healthcare is our third assurance. We'll make sure everyone is treated well. There will be mohalla clinics in every community and village. A multispecialty hospital will be created out of a district hospital. Every citizen in our nation is entitled to free medical care. Insurance will not be used as the foundation for treatment because this is a major fraud. We're going to build infrastructure. Mr. Kejriwal announced that ₹ 5 lakh crore rupees will be allocated for healthcare. </p>
