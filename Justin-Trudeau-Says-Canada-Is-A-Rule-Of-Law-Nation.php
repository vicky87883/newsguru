	<p>Following Trudeau's claims in September of last year on the "potential" involvement of Indian operatives in the murder of Nijjar, a 45-year-old Khalistan separatist, the ties between India and Canada came under extreme strain.</p>
	<h5>India Dismisses Trudeau's 'Absurd' Charges</h5>
	<p>Trudeau's accusations have been brushed off by India as "absurd" and "motivated." The death of Nijjar in Canada, which is headed for elections, is primarily the result of domestic politics, according to External Affairs Minister S Jaishankar, and it has nothing to do with India.

He claimed that some pro-Khalistan citizens are taking advantage of Canada's democracy to form a lobby and turn the country into a voting machine. According to him, certain parties in Canada rely on politicians who support Khalistan, and the ruling party does not hold a majority in Parliament.


"It is creating issues for them (Canada), for us, and for our relationship. We have persuaded them multiple times not to grant visas, legitimacy, or political space to such people," Jaishankar stated.
</p>
	<p>However, Jaishankar stated that the Canadian government has done nothing and that India had requested the extradition of 25 individuals, the majority of whom supported Khalistan, but that request had been ignored.

"Canada did not provide any evidence. In certain situations, they withhold any proof from us, and police departments likewise don't work with us. In Canada, they are politically compelled to place the blame on India. They engage in vote-bank politics as Canada prepares for elections, the minister claimed.
</p>
