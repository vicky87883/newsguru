	<h5>What Police Said?</h5>
	<p>Without providing further information, Canadian police stated they had collaborated with US law enforcement after three Indian nationals were detained in relation to the murder.

The cops hinted that there might be more arrests to come. Assistant Commissioner David Teboul, the commander of the Royal Canadian Mounted Police (RCMP) in the Pacific region, stated on Friday that while he would not comment on the purported connections between the three men detained and Indian officials, the force is "investigating connections to the government of India."
</p>
