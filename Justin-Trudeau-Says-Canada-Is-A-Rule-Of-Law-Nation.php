<p>Toronto: Canadian Prime Minister Justin Trudeu declared on Saturday that his country is a "rule-of-law country" in response to the arrest of three Indian nationals in connection with the murder of Khalistani terrorist Hardeep Singh Nijjar. He added that there is an investigation going on, and it is not restricted to the three arrests.</p>
	<p>On June 18, 2023, Hardeep Singh Nijjar, a purported Canadian citizen, was shot and killed outside a gurdwara in Surrey, British Columbia.

On Friday, three Indian nationals living in Edmonton—Karan Brar, 22, Kamalpreet Singh, 22, and Karanpreet Singh, 28—were charged with first-degree murder and conspiracy to commit murder.


Regarding the arrests that took place on Saturday at a Toronto event honoring Sikh heritage and culture, Prime Minister Justin Trudeau stated, "This is important because Canada is a rule-of-law country with a strong and independent justice system, as well as a fundamental commitment to protecting all its citizens."
</p>
