							<p>Democrat Mornes pointed to growing rent and food prices and stated, "Now that he's in the office, the cost of living has risen out of control, and there's nothing being done about it." She stated, "He just chooses to do other things," which makes her think Biden is capable of more.

Under Trump's leadership, the economy lost 2.7 million jobs as a result of the pandemic. Yet, the lockdowns during the pandemic also significantly reduced inflation. In the meantime, many people felt that Trump had improved their lot because of historically low borrowing rates and massive government stimulus supported by deficits.
</p>
