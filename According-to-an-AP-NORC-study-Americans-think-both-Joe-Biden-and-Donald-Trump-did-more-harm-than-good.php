							<p>"I did very well during those four years—considering the price of gas, the price of groceries, and the economy," 60-year-old Republican from Texas Christina Elliott said of the Trump administration. "I didn't have to worry about going to the grocery store and blowing half of my paycheck."</p>
							<p>Elliott expressed disapproval of Trump's treatment of abortion and stated that the former president should "just learn how to be tactful and shut his mouth" in regards to his comments.

"Aside from that, I performed admirably throughout the Trump administration," she continued.
</p>
