							<p class=font-large><mark>According to a recent study, Americans are unhappy with both Biden and Trump's records because they feel they accomplished more harm than good on important subjects. Biden receives criticism for how he has handled immigration and the economy, while Trump is under assault on a number of fronts.</mark></p>
							<p>There's a reason why people think neither former President Donald Trump nor Vice President Joe Biden has much to talk about in terms of their own records, which explains why they spend so much time criticizing one another. Most Americans believe that both of them accomplished more harm than good on important subjects during their time in the White House.</p>
