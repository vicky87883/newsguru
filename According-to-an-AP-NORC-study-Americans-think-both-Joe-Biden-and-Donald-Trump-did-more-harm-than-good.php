							<h5 style="color:red;">Trump has a different problem.</h5>
							<p>Though the general feeling of harm is somewhat broader, the former president is not questioned about any subjects where more than half of Americans believe he did more harm than good. About 50% of Americans believe that his administration had a more negative impact than positive one on issues like foreign policy, voting rights, election security, abortion legislation, and climate change.</p>
							<p>Republican Catherine Scott, who recently relocated to New York from Florida, expressed anxiety about Trump's foreign policy strategy.

The 30-year-old Scott stated, "I know that some people really admire Trump's ability to be a spitfire and just say whatever is on his mind." However, Scott said, "I don't think he has all the foresight to understand that might not always be the thing to do," referring to Trump's praise for autocrats like Russian President Vladimir Putin.
</p>
							<p>Jobs are generally the best subject for both Biden and Trump. Here, Trump has a little advantage: 36% believe Biden's presidency was helpful, while almost half believe Trump's helped. Additionally, about half of Americans believe that Trump's administration has improved immigration, and four out of ten believe that it has reduced the cost of living.</p>
