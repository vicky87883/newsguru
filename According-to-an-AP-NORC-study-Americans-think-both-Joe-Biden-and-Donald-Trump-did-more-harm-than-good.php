							<p>However, the two contenders' areas of weakness differ. According to Biden, there is general dissatisfaction with two issues: immigration and the economy. Meanwhile, a sizable portion of the electorate believes that Trump has hurt the nation on a number of subjects.</p>
							<p>According to a recent AP-NORC Center for Public Affairs Research poll, over half of US adults believe that Biden's presidency has harmed immigration and the cost of living, while nearly half believe that Trump's presidency has harmed voting rights, election security, ties with other nations, abortion laws, and climate change.</p>
