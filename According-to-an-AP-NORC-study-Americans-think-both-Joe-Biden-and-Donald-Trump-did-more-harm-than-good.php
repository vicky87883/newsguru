							<p>Regarding all other issues, just about three out of ten Americans believe that either Biden or Trump made a significant or minor contribution to the nation. However, Republicans generally perceive Trump's presidency to be more beneficial than Biden's, especially on matters where Biden has attempted to draw attention to his accomplishments.

For instance, just roughly 50% of Democrats believe that Biden's administration has reduced health care costs or the effects of climate change. Regarding abortion regulations, 77% of Democrats believe that Trump's administration was at least somewhat detrimental. However, only roughly 4% of Democrats believe that Biden's presidency was helpful, and a comparable percentage believe it hasn't had any impact.
</p>
							<p>On the other hand, about 80% of Republicans believe that Trump's administration has improved border security, immigration, and the cost of living.</p>
