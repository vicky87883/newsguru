							<p>Following the pandemic, Biden stimulated the economy by providing extra funding, which led to a rise of 15.2 million jobs during his administration. However, many analysts believe that supply chain problems, Russia's war in Ukraine, and Biden's aid package have all led to rising inflation, which has harmed the Democratic Party's popularity ratings.</p>
							<p>Part of the reason for Trump's edge on immigration and living expenses is Democrats' disinterest in Biden's performance. For instance, almost one-third of Democrats believe that Joe Biden's presidency increased living expenses, while another third believe that Biden had no effect at all. Merely 33% of Democrats believe that Biden's presidency reduced the cost of living. Three out of ten Democrats believe that Biden's presidency improved immigration and border security; a comparable number believe that it had the opposite effect; and four out of ten believe that it had no effect.</p>
							<p>Pennsylvania Democrat Nadia Stepicheva, 38, is dissatisfied with Biden's immigration policies.

Stepicheva remarked, "The issue is that I really don't like illegal immigration." Even if they enter the country illegally, she believes that those who do so should be permitted to work so that government funds aren't utilized to house and care for them.
</p>
							<p>Although Stepicheva has always supported Democrats and their ideals, she added, "I feel like the last four years, too much money has been spent on immigration and student loan forgiveness." Regarding who she will vote for in November, she stated that she is undecided.

However, independents also think poorly of Biden on these matters: About half of independents believe that the country's cost of living has increased under Biden's presidency. Approximately 40% of independent voters believe that Biden's presidency has harmed the nation's foreign policy and health care costs.
</p>
