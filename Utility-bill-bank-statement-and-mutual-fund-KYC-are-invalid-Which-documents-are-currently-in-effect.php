<p>Due to modifications made by the Securities and Exchange Board of India (Sebi) to the Know Your Client (KYC) requirements, investors in mutual funds can now provide a different range of documents as confirmation of identity or address as of April 1. Before making any investments, all mutual fund investors must complete the KYC procedures. </p>
