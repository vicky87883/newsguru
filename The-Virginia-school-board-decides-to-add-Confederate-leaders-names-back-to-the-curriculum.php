	<p>Early on Friday morning, the Shenandoah County, Virginia, school board approved a plan to rename two public schools after prominent Confederate military figures.
	</p>
	<p>
The 5-1 vote overturns a 2020 board decision that had changed the names of schools associated with Stonewall Jackson, Robert E. Lee, and Turner Ashby—three leaders of the Southern states who supported slavery during the Civil War.
	</p>
	<ins class="adsbygoogle"
     style="display:block"
     data-ad-format="autorelaxed"
     data-ad-client="ca-pub-6958761602872755"
     data-ad-slot="1082574772"></ins>
<script>
     (adsbygoogle = window.adsbygoogle || []).push({});
</script>
	<p>
Stonewall Jackson High School will once again be the name of Mountain View High School. Ashby-Lee Elementary School will once again be known as Honey Run Elementary School.
	</p>
	<p>
After George Floyd's murder by a Minneapolis police officer, which sparked a national conversation about race, the board removed their names. Several cities have removed Confederate symbols and statues of Confederate generals in response to cries for racial fairness and equity.
	</p>
	<p>
However, the Alliance for Better Schools, a conservative organization, petitioned Shenandoah County school officials to restore Jackson, Lee, and Ashby's names. A copy of the group's April 3 letter to the board can be found online. "We believe that revisiting this decision is essential to honor our community’s heritage and respect the wishes of the majority," the coalition stated.
	</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
     data-ad-slot="3917672390"></ins>
<script>
     (adsbygoogle = window.adsbygoogle || []).push({});
</script>
	<p>
In 2022, the board discussed a similar move, but a tie vote prevented it from passing.

Minutes from a July 9, 2020 meeting show that the previous iteration of the board voted 5-1 to alter the names four years prior. "Criticizing racism and affirming the division's commitment to an inclusive school environment for all," according to the minutes, was the resolution's stated purpose.
	</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
     data-ad-slot="3917672390"></ins>
<script>
     (adsbygoogle = window.adsbygoogle || []).push({});
</script>
	<p>
The present members said that the 2020 board decided in a hurry and without consulting the community as needed. Prior to the board's vote on Thursday, some 80 persons spoke, with over 50 of them opposed to the original names being restored.

One girl addressed the board directly, saying, "I am a Black student, and if the names are restored, I would have to represent a man that fought for my ancestors to be slaves." She went on, saying, "I think it is unfair to me that restoring the names is up for discussion."
	</p>
	<p>
Confederate imagery has stoked sharp social divisions across the country in the previous 10 years.
	</p>
	<p>
The June 2015 anti-Black mass shooting at Charleston, South Carolina's Emanuel African Methodist Episcopal Church sparked heated discussions on the public flying of the Confederate flag and Confederate memorials. That year, South Carolina officials decided to take the Confederate flag down from the state Capitol grounds.
	</p>
	<p>
A violent "Unite the Right" event took place in Charlottesville, Virginia, two years later, drawing hundreds of neo-Nazis and white supremacists. Part of the reason they invaded the college town was to voice their opposition to the proposed removal of a monument of Lee from Market Street Park, which used to be known as Lee Park.
	</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
     data-ad-slot="3917672390"></ins>
<script>
     (adsbygoogle = window.adsbygoogle || []).push({});
</script>
	<p>
Following Floyd's murder and the widespread anti-racism demonstrations that followed, the legacy of the Confederacy once again became a major topic of discussion in the country. A count by the Southern Poverty Law Center indicates that in 2020, at least 160 public Confederate emblems were removed or relocated from public areas.

Lecia Brooks, chief of staff of the SPLC, stated in a statement at the time that "these racist symbols only serve to uphold revisionist history and the belief that white supremacy remains morally acceptable." "For this reason, we think that all representations of white supremacist should be eliminated from public areas."
	</p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
