</script>
	<p>
The vote in Shenandoah County occurs at a time when conservative organizations nationwide are stepping up their opposition to initiatives aimed at addressing race in American educational settings. These initiatives include attempts to restrict classroom discussion of racial identity, outlaw racial-themed library books, and prevent diversity initiatives.</p>
