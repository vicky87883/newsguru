<p><mark>After George Floyd's assassination, a Virginia school board removed the names of prominent Confederate military commanders from two schools. A motion for reintroducing the names was accepted by the board four years later.</mark></p>
	<ins class="adsbygoogle"
     style="display:block; text-align:center;"
     data-ad-layout="in-article"
     data-ad-format="fluid"
     data-ad-client="ca-pub-6958761602872755"
