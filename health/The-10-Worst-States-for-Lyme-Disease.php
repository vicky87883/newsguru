<div class=single-excerpt>
<p>Although more "exotic" infectious diseases like Zika, West Nile, and chikungunya have surfaced recently, Lyme disease remains the most prevalent vector-borne sickness in the United States.
</p>
<p>
The Centers for Disease Control and Prevention reported about 35,000 confirmed and probable cases of Lyme disease in the US in 2019, a 4% rise from the previous year. However, the real yearly case count is probably higher: In a 2021 analysis of insurance claims data, writers from the CDC calculated that between 2010 and 2018, over 476,000 Americans had a Lyme disease diagnosis and treatment, with the important caveat that individuals might be treated presumptively for a condition.
</p>	
<p>As of 2019, a total of 15 states and the District of Columbia were classified as having "high incidence" of Lyme disease, which is defined by the CDC as having an average incidence of at least 10 confirmed cases per 100,000 persons for three reporting years. The disease is typically most heavily concentrated in the Northeast, mid-Atlantic, and Upper Midwest. According to the CDC, there were 432 counties in 2019 with incidence rates of 10 or more confirmed cases per 100,000, up from 324 in 2009.
</p>
<p>The CDC reports that if Lyme disease is not treated, it can result in a variety of symptoms such as fever, rash, arthritis, and facial paralysis. The bacteria that causes Lyme disease is transmitted through the bite of infected blacklegged or western blacklegged ticks. Antibiotic therapy and early diagnosis are advised to avoid later-stage disease. Removing a tick as soon as feasible is advised by guidelines in order to lower the risk of illness.
</p>
<p>
The most recent year for which the CDC has released data is 2019, and these ten states have the highest incidence rates of Lyme disease per 100,000 people.
</p>
