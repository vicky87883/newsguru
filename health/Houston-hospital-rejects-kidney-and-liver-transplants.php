	<p><mark>Houston — According to media reports, a Houston hospital has suspended its kidney and liver transplant programs after learning that a physician had falsified documents for patients seeking liver transplants.</mark>
	</p>
	<p>
According to a statement from Memorial Hermann-Texas Medical Center, "inappropriate changes... effectively inactivated the candidates on the liver transplant waiting list," the Houston Chronicle was told. "As a result, these patients were not eligible to accept offers of organ donation while they were inactive."
	</p>
<p>
According to the medical center's statement, the physician acknowledged changing patient records. 
</p>
	<p>He was identified by the New York Times as transplant physician Dr. J. Steve Bynon Jr., citing officials. He works at the University of Texas Health Science Center in Houston, according to the newspaper, and has been in charge of Memorial Hermann's abdominal transplant department since 2011.

When The Times called Bynon on Thursday, he directed inquiries to UTHealth Houston, which failed to respond. He denied reports that he had acknowledged manipulating records.
</p>
	<p>On April 3, the hospital terminated the liver transplant program due to "irregularities" with the donor acceptance standards. The hospital withheld information about the investigation's findings, which included issues with data entered into a database used to match patients to donor organs.

According to the hospital, the "irregularities" were restricted to liver transplants; nonetheless, kidney transplants were discontinued due to leadership similarities between the programs.
</p>
	<p>According to a statement from the organization, the U.S. Department of Health and Human Services is aware of the claims and is conducting an inquiry.

The statement stated, "We are committed to protecting patient safety and ensuring that all patients have equal access to organ transplant services." "To safeguard the security and integrity of the donation of organs and transplantation system, HHS will pursue all appropriate enforcement and compliance actions."
</p>
	<p>Organ Procurement Transplantation Network data indicates that Memorial Hermann has experienced a rise in the number of liver transplant candidates who pass away while waiting for a transplant or who become too ill to receive one in recent years.

Based on the statistics, four patients passed away or became too sick for a transplant in 2021, 11 in 2022, 14 in 2023, and five so far in 2024.


Similarly, How long the programs will be closed is a question Hermann has not answered.

The hospital announced that it was coordinating treatment with patients' families and that it was reaching out to the 346 patients on the kidney transplant waiting list and the 38 patients on the liver program transplant waiting list.
</p>
	<p>The United Network for Organ Sharing states that when the transplant program is discontinued, patients on waiting lists do not get offers of organs; instead, they accumulate waiting time. Patients may also transfer their wait time to another program or be on numerous transplant waiting lists; however, each institution has its own standards for assessing and accepting transplant candidates.

Transplant programs are also available in Houston at Houston Methodist, Baylor St. Luke's Medical Center, and the Michael E. DeBakey Veterans Affairs Medical Center.
</p>
