	<h5 style="color:red;">4. Stay away from issues</h5>
	<p>Stay away from issues. Speaking with a sympathetic friend about a situation can be quite helpful. However, depression can make people excessively rehashing, blaming, and complaining about issues. It might help you stay fixated on your problems.

Speaking with caring people about your ideas and feelings is OK. But avoid talking only about your concerns. Talk about positive topics as well. Attempt to replace your pessimistic thoughts with more optimistic ones. This can assist in elevating your mood.
</p>
