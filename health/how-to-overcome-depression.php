	<h5 style="color:red;">5. Take note of the positive aspects</h5>
	<p>Depression has an impact on one's perspective on life. Things can appear gloomy, pessimistic, and hopeless. Aim to find three positive aspects of each day in order to change your perspective. You will notice more good as you increase your awareness of what's good.</p>
	<p>Above all, be kind and compassionate to yourself if you're experiencing depression. It's comforting to know that you're not struggling through this alone. Have patience with yourself. Healing from depression takes time.</p>
