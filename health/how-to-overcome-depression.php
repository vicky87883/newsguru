	<h5 style="color:red;">2. Take nutritious foods and stay hydrated</h5>
	<p>There are some depressed individuals who don't feel like eating. Some people might eat too much. However, the food you eat might impact your energy and mood. Thus, eating healthfully is important when dealing with depression. That means eating a wide range of fruits, vegetables, and whole grains for most people. Eat fewer simple carbohydrates and sugar-filled items, such as desserts and "junk" food. Avoid fasting for extended periods of time. Have a light, healthful meal even if you're not hungry. Additionally, remember to drink plenty of water to stay hydrated. Steer clear of sugary and caffeinated beverages whenever you can. </p>
