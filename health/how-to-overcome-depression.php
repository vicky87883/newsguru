	<h5 style="color:red;">3. Be yourself</h5>
	<p>Your ability to be creative and have fun may appear limited when you are depressed. However, engaging in activities that spark your creativity can be beneficial. Draw, paint, or doodle. Cook, bake, or sew. Compose, dance, or write music. Play with a pet, or converse with a friend. Look for a reason to chuckle. See a humorous film. Engage in activities you find enjoyable. Even somewhat. That helps in the recovery from depression.</p>
