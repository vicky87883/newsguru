	<p>Five Tips to Get Over Depression on Your OwnThe best course of action if you're experiencing depression is to see a therapist. You can take steps to improve yourself in order to benefit the most from your therapy.</p>
<p>
These five actions will help you feel better. Simple as it might appear, they can be really beneficial.
</p>
	<h5 style="color:red;">1. Workout</h5>
	<p> Every day, go for a quick walk for 15 to 30 minutes. You could also stretch, perform yoga, dance, or play a sport. It's possible for sad people to have little desire to exercise. However, try to get yourself to do it. Ask a friend to work out with you if you need extra motivation. Any kind of activity will improve your mood. Keep it going</p>
