<p>During his career, Bloomquist also had three starts in the ARCA Menards Series in 1991, one start in the NASCAR Craftsman Truck Series at Eldora in 2013, and a cameo on a CBS television broadcast of an SRX race at Knoxville Raceway in 2021.
</p>
