	<p>New Delhi: The Reserve Bank of India has immediately prohibited Kotak Mahindra Bank from onboarding new clients online and from issuing new credit cards due to data security concerns and inadequate IT infrastructure. Nonetheless, the bank is still able to assist its present customers, which includes credit card holders.</p>
	<p>In the exercise of its authority under Section 35A of the Banking Regulation Act, 1949, the Reserve Bank of India has today ordered Kotak Mahindra Bank Limited (henceforth referred to as "the bank") to immediately stop (1) onboarding new customers through its online and mobile banking channels and (ii) issuing new credit cards. However, the bank will keep offering services to its current customers, which includes those who use credit cards," a statement from the central bank stated.</p>
	<p>The RBI stated that the measures are motivated by "significant concerns arising out of Reserve Bank's IT Examination of the bank for the years 2022 and 2023 and the continued failure on part of the bank to address these concerns in a comprehensive and timely manner" .
</p>
