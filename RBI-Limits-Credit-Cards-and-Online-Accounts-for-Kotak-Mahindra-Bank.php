	<p>When Uday Kotak established Kotak Capital Management Finance, an investment and financial services firm, in 1985, Kotak Mahindra's journey officially began. The business was renamed Kotak Mahindra Finance after Anand and his father Harish Mahindra made investments in it the next year.</p>
