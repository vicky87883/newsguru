	<p>"Serious deficiencies" were found in Kotak Mahindra Bank's data security and IT inventory management systems. IT inventory management, patch and change management, user access management, vendor risk management, data security and data leak prevention plan, business continuity and disaster recovery rigor and drill, etc. were found to have serious flaws and non-compliances. In violation of regulatory norms, the bank was found to have inadequate information security governance and IT risk management for two years in a row, according to the RBI.</p>
	<p>"During the subsequent assessments, the bank was found to be significantly non-compliant with the Corrective Action Plans issued by the Reserve Bank for the years 2022 and 2023, as the compliances submitted by the bank were found to be either inadequate, incorrect or not sustained," it stated.</p>
