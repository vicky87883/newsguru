	<p>According to the central bank, in the best interests of its clients, it has chosen to impose some restrictions on the bank. "The Reserve Bank, therefore, has decided to place certain business restrictions on the bank as mentioned above, in the interest of customers and to prevent any possible prolonged outage which may seriously impact not only the bank's ability to render efficient customer service but also the financial ecosystem of digital banking and payment systems," said the RBI.</p>
	<p>The central bank stated that following an audit and corrective actions, the curbs will be reviewed. "After a thorough external audit, to be commissioned by the bank with RBI's prior approval, is completed and all deficiencies found in the external audit and RBI Inspection observations are addressed to the Reserve Bank's satisfaction, the current restrictions will be reevaluated. Furthermore, the Reserve Bank may take additional regulatory, supervisory, or enforcement action against the bank without prejudice as a result of these limits, the RBI stated.</p>
