<p>
The RBI observed that the bank's digital banking channels had experienced frequent breakdowns and inconvenienced consumers as a result of the lack of a strong IT infrastructure.
</p>
	<p>"The bank's Core Banking System (CBS) and its online and digital banking channels have experienced frequent and significant outages in the last two years due to a lack of a strong IT infrastructure and IT Risk Management framework. The most recent outage was a service disruption on April 15, 2024, which caused significant inconveniences for customers. Because the bank failed to develop IT systems and controls in line with its expansion, it was determined that it was materially lacking in the development of the operational resilience that was required," the statement read.</p>
	<p>"To improve the bank's IT resilience, the Reserve Bank has been in constant high-level engagement with it over the last two years, but the results have been far from sufficient. The volume of the bank's digital transactions, notably those involving credit cards, has also been noticed to have grown quickly recently, the RBI said. This has put additional pressure on the IT infrastructure.</p>
