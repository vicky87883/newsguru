Kotak Mahindra Finance Ltd. became the first non-banking finance company in India to become a bank in 2003 after obtaining a banking licence from the RBI. The bank today provides a wide range of services, including investment banking, mutual funds, stock broking, insurance, and commercial banking.</p>

