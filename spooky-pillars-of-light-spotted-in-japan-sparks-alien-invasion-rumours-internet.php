	<p>A user going under the handle Maashii posted the first images of the bright columns on social media after she saw them over the seaside town of Daisen. A similar photo was then shared by user totoro8201, who claimed to have seen the light pillars from Nariishi beach, which is around 12 kilometers east of Daisen. The pictures quickly became well-known, leading to a lot of conjecture over their origin.</p>
	<p>The fact behind these bright pillars is rooted in the long-standing activity of squid fishing, in contrast to the cosmic hypotheses that are making the rounds on the internet. A sunnyskyz.com report explains that Japanese fishermen use special lights called "Isaribi" to draw in bigger catches, which is an essential tactic in squid fishing. These lights provide a very dramatic visual impact, especially in certain lighting settings.</p>
