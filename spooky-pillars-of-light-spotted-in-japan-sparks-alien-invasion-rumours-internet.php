	<p>Captivating photos of a brilliant show in the night sky over Tottori, Japan, have caused a stir of conjecture on social media. Nine pillars of light spreading over the horizon were seen in these photographs, which at first many thought were an alien or cosmic event. But a new investigation has revealed the real source of this amazing exhibition.</p>
	<p>The phenomena that was seen in Tottori on May 11th has been described as "fish-attracting light pillars," or "Isaribi Kochu," a well-known occurrence in Japan, according to a report by Mothership. The captivating lights aren't coming from space; instead, they're the product of local fisherman using realistic fishing methods.
</p>
