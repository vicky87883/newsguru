	<p>The Delhi High Court criticized the city government on Friday for failing to provide books to students enrolled in MCD schools, stating that Arvind Kejriwal's continued appointment as chief minister despite his arrest serves political rather than national interests.

The Delhi Minister of Urban Development, Saurabh Bharadwaj, was also criticized by the high court for allegedly turning a blind eye to the predicament of students. According to the court, "this is arrogance of power at its highest," PTI quoted it.
</p>
	<p>Advocate Ashok Agarwal went in court during a Public Interest Litigation (PIL) to inform the judge that pupils at MCD School have not received supplies like as books, notebooks, writing instruments, uniforms, etc.

Arvind Kejriwal was arrested and placed in judicial custody at Delhi's Tihar Jail as part of an investigation into the purported excise policy scandal. On March 21, he was taken into custody by the Enforcement Directorate.
