	<p>Chief Minister Arvind Kejriwal and his government were "exposed" by the high court during a hearing held the previous day, according to Delhi Bharatiya Janata Party (BJP) leader Virendra Sachdeva on Saturday.

The Delhi High Court criticized the Arvind Kejriwal-led administration on Friday, stating that the Chief Minister of Delhi had prioritized his own interests over that of the country by refusing to step down in the face of his imprisonment.
</p>
	<p>"The court exposed (Delhi Chief Minister) Arvind Kejriwal and his administration in addition to rebuking them. "He [Kejriwal] is becoming blind for power and corruption...and doing selfish politics," the Delhi High Court said in a very stern statement yesterday, Virendra Sachdeva told PTI.

He went on to say that although books and uniforms are missing from over two lakh MCD schools, "Arvind Kejriwal will not leave the CM seat because he is hungry for power".
</p>
