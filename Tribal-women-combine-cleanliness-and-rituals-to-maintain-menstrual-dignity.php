							<p><mark>Pune: To protect a woman's dignity and safety during her monthly periods, the tribal areas of Gadchiroli have finally combined traditional menstrual customs with modern hygienic practices.</mark></p>
                            <p>
While most women in cities could hardly imagine going without sanitary pads, tampons, or menstruation cups, women in the 600 villages of Gadchiroli were forced to live apart from their families for five to six days each month in dark, wet mud shelters known as kurmas.
</p>
<p>An ASHA worker said that one of the villages she worked in at Dhanora taluka during the Udaan training programme, a woman was bitten by a snake as she slept in a mud kurma far away from her home during her period cycle. “We realised proper concrete homes with bathroom facilities were needed to replace mud rooms to keep women safe while menstruating.”
Meanwhile, district surveillance officer of Gadchiroli Dr Vinod Mhashakhetri said that kurma huts made of mud and haystacks were located outside villages in forests either near a lake or a river in tribal areas to allow women time away for rest during their period cycle. However, it became a hassle over time.</p>
<p>Rather than questioning customs of women being segregated during menstruation, we decided to improve their quality of life. Despite the matriarchal structure of most tribal cultures, women nevertheless feel the need to spend those few days apart from their families. He warned that living in old kurma huts exposes them to snake and insect bites, wild animals, and a host of other dangers.</p>
<p>Former Gadchiroli district health officer Dr. Dawal Salve stated, "I realized not all Kurma households were the same when I was placed in the district in 2022–2024. They were divided into smaller groups for usage. One thing in common with all of the kurma huts was that they were devoid of running water, light, and restrooms; as a result, women were leaving themselves vulnerable to infections of the vagina and urinary system as well as the typical monsoon snake bites.</p>
<p>"We cannot dissuade the elderly tribal population to give up their age-old traditions," stated Dr. Pratap Shinde, the district health officer for Gadchiroli. Thus, we made the decision to attempt to change the customs by updating the kurma rooms. Lights were installed, and houses with bathrooms and running water were constructed. This has been the district health administration's practice since 2018. Additionally, NGOs and private contractors who construct secure concrete kurma homes assist us.</p>
<p>According to Mhashakhetri, the government and UNICEF launched the Udaan project in 2018. "We made the decision to teach young women and girls about menstruation. The buildings were now known as women's rest homes. We constructed 17 of these houses with running water, electricity, and restrooms. We also took advantage of the chance to instruct ladies on the proper usage and disposal of sanitary pads.</p>
<p>A teacher involved in the Udaan initiative in 2018, Smita Pote, said it was hard to persuade tribal women that they didn't need kurma homes. "We made the decision to update the current kurma rooms. We also observed a large number of educated, young women resisting it or changing their habits to fit in with the modern world. These days, the new Kurma dwellings are found in villages. In this approach, we persuaded the ladies that accepting a better option was preferable than questioning their customs, as doing so is not feasible.</p>
