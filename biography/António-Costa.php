<div class=single-excerpt>
	<p>A Portuguese lawyer and politician, António Luís Santos da Costa GCIH (born July 17, 1961)[3] was the 118th prime minister of Portugal from 2015 to 2024, leading the XXI (2015–2019), XXII (2019–2022), and XXIII Constitutional Governments (2022–2024). After that, in December 2024, Costa will assume the role of <a href="https://www.newsguru.live/current-affairs/article.php?id=4">President of the European Council</a>.[4]
</p>
<p>
He has previously served as Lisbon's mayor from 2007 to 2015, Minister of Parliamentary Affairs from 1997 to 1999, Secretary of State for Parliamentary Affairs from 1995 to 1997, Minister of Justice from 1999 to 2002, and Minister of Internal Administration from 2005 to 2007. In 2014, he was chosen as the Socialist Party's Secretary-General, a position he kept until the beginning of 2024.[5]</p>
<p>Following an inquiry involving members of his cabinet regarding allegations of corruption and wrongdoing in the management of lithium mining and hydrogen projects in the nation, Costa announced his resignation on November 7, 2023.[6][7]
</p>
<p>
Portugal's President chose to dissolve the legislature and scheduled a quick election, which was conducted on March 10, 2024. Up until the swearing-in of his replacement, Luís Montenegro, the leader of the opposition, on April 2, 2024, Costa served as caretaker prime minister.[8][9]</p>
<div><strong>Early life and education</strong></div>
<p>Costa, the son of writer Orlando da Costa and journalist Maria Antónia Palla, was born in Lisbon, Portugal, in 1961. Orlando da Costa was half Indian and half French-Mozambican; his father was born into a Goan family in Maputo, Mozambique.[10][11] Costa is referred to as Babush, a young, cherished relative in Konkani, with great respect in Goa.[12]
</p>
<p>
In the 1980s, after earning his law degree from the University of Lisbon, Costa entered politics and won a seat on the city council as a socialist representative. After serving his mandatory time in the military until 1987[13], he briefly practiced law starting in 1988 before deciding to go into politics full-time.[14]
</p>
