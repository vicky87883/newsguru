	<p>There is a large fire in Nainital, according to Pushkar Singh Dhami. "We have a threat from the forest fires. This fire is quite large. We are striving to meet every basic demand. We have requested assistance from the Army," he declared.</p>
<p>According to Pushkar Singh Dhami, the government has taken action to put out the fire. "Today, I have a meeting scheduled in Haldwani. We've had a meeting about this in Dehradun as well. We'll work to contain the fire as quickly as we can," he continued.</p>
<p>The Nainital High Court Colony is at danger due to the forest fire. Dhami has directed the departments to put out the fire after they had conferred with one another.
</p>
	<p>The colony is located close to the Pines region, and that is where the fire has spread. It had an impact on traffic flow as well.
Rahul Anand, the executive officer of Nainital Municipal Corporation, stated in the interim: "We have learned that IAF helicopters are requesting authorization to utilize water from Nainital to put out the fire. We stopped boating in the lake for the day so that plans could be made. There have been numerous reports of fires in Nainital, including one that occurred not far from the district's Air Force Station. The Air Force Station's safety was the primary consideration when helicopters were called in to put out the fire.
</p>
