	<p>In addition, images of Chinese coastguard ships and Jiangdao-class corvettes were displayed; however, the specific location of the photos was not specified.
As of Friday at 6 a.m. (2200 GMT), the ministry reported that it has identified 49 Chinese military aircraft, 19 naval vessels, and seven coast guard ships. 28 of the planes crossed the median line across the strait, which China claims it does not recognize but which was once an unofficial border.
A chart given by the ministry showed that the nearest Chinese aircraft to the coast of Taiwan was located 40 nautical miles (74 km) from the northern city and navy base of Keelung.
</p>
