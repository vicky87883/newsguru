	<p>The People's Liberation Army's Eastern Theatre Command stated that the goal of the "Joint Sword - 2024A" drills was to "test the ability to jointly seize power, launch joint attacks, and occupy key areas".
A spokesman for China's defense ministry, Wu Qian, stated that "this action is completely reasonable, legal, and necessary to combat the arrogance of 'Taiwan independence' and deter the interference and intervention of external forces."
</p>
	<div><strong>FIRST ISLAND CHAIN</strong></div>
	<p>A senior Taiwan security official told Reuters that multiple Chinese bombers practiced how to take "total control" of areas west of the so-called first island chain by simulating assaults on foreign warships close to the eastern end of the Bashi Channel, which divides Taiwan from the Philippines.</p>
