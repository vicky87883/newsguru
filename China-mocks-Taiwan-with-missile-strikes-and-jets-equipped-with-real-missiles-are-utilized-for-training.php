	<p>Lai has been rejected every time he has offered to hold discussions with China. He opposes Beijing's claims of sovereignty, saying that only the people of Taiwan can determine their own destiny.
Taiwan is accustomed to China's military threats, so there hasn't been any unnecessary fear on the island as a result of the most recent drills; business as usual continues.
Taiwanese media has reported on the exercises, but they have also devoted a great deal of space to the continuing drama around disputed legislative amendments that have prompted thousands of protesters to go to the streets.
</p>
