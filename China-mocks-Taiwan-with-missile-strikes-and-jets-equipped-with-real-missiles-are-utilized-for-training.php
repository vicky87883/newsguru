	<p>According to Chinese official broadcaster CCTV, the Chinese ship Nantong conducted battle readiness patrol and practical drill missions in the Taiwan Strait, with the Taiwanese ship Zheng He trailing 0.6 nautical miles behind.
The 7th Fleet of the U.S. Navy, according to a public relations officer, takes "very seriously" its obligation to prevent aggression in the Indo-Pacific and is keeping an eye on "all of the activities" in the area.
Despite not having an official diplomatic relationship since Washington publicly recognizes Beijing, it is legally obligated to give Taiwan the resources it needs to defend itself and is the island's main international supporter.
</p>
	<p>Taiwan's Foreign Minister Lin Chia-lung declared in a speech in Taipei that the island would resist external pressure.
"We will not make any concessions because of this Chinese military exercise, because it concerns the development of democracy in Taiwan," he stated.
</p>
