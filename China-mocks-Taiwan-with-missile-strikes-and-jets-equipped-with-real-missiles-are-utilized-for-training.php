	<p><mark>May 24, Beijing/Taipei (newsguru) - Beijing has claimed that the exercises were carried out to punish Taiwan's new president, Lai Ching-te. On Friday, the country conducted fake missile strikes and sent fighter jets carrying live missiles along with bombers, according to state broadcaster CCTV.
China proved its capacity to "seize power" and control critical areas of Taiwan, it said, as the bombers set up various strike formations in waters east of Taiwan and carried out simulated attacks in coordination with navy boats.
</mark></p>
