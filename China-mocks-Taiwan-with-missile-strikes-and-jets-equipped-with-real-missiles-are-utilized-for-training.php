	<p>
The region that encloses China's coastal seas and stretches from Japan to Taiwan, the Philippines, and Borneo is referred to as the first island chain.
Given the delicate nature of the issue, the official spoke anonymously but stated that many Chinese coastguard boats conducted "harassment" maneuvers near Taiwan's east coast, which included fictitious inspections of civilian ships.
	</p>
	<p>
The coastguard of China reported that on Friday, it had carried out "law enforcement drills" in waters east of Taiwan, with an emphasis on training in identification and verification as well as warning and rejection.
</p>
