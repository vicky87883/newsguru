	<p>Just three days after taking office on Monday, the two days of drills in the Taiwan Strait and around groupings of Taiwan-controlled islands near the Chinese coast began. According to a Taiwanese official, the drills included simulated bombings of foreign vessels. Taiwan has denounced the acts of China.
China calls Lai a "separatist" and regards Taiwan, which is democratically governed, as its own territory. It harshly criticized his inauguration speech, in which he declared that the two sides of the strait were "not subordinate to each other" and urged Beijing to cease its threats.
</p>
