	<div><strong>'SACRED WEAPONS'</strong></div>
	<p>On its WeChat social media account, the Chinese Theatre Command posted an animated video on Friday depicting missiles being fired at Taiwan from the air, sea, and land. The missiles then explode into fiery balls that strike Taipei, Kaohsiung, and Hualien. Later, according to CCTV, China used several dozen missiles to stage simulated missile attacks on Taiwan.
The final words of the video, "Sacred weapons to kill independence," were read in red and were printed in the traditional Chinese characters used in Taiwan.
Taiwan's military has gathered in order to keep an eye on and observe Chinese forces.

The defense ministry of Taiwan released images of F-16s flying around with live missiles on Friday.
</p>
