Senior Taiwanese officials, analysts, and regional diplomats pointed out that although the drills' scope is smaller than that of the comparable exercises in 2022—which were much anticipated by both foreign and Taiwanese officials—they nevertheless increase the possibility of mishaps or errors in judgment.
</p>
	
